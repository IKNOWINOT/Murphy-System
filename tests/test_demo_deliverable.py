class TestDemoDeliverableUsageLimits:
    """Tests for anonymous and free-tier usage limits."""

    def test_anon_limit_is_5(self):
        """Anonymous users must be blocked after exactly 5 downloads."""
        from src.subscription_manager import SubscriptionManager
        mgr = SubscriptionManager()
        fp = "demo_anon_test_fingerprint_abc123"
        for i in range(5):
            result = mgr.record_anon_usage(fp)
            assert result["allowed"] is True, f"Should be allowed on attempt {i+1}"
        # 6th attempt must be blocked
        result = mgr.record_anon_usage(fp)
        assert result["allowed"] is False
        assert result["remaining"] == 0
        assert result["tier"] == "anonymous"

    def test_free_registered_limit_is_10(self):
        """Free registered users must be blocked after exactly 10 downloads."""
        from src.subscription_manager import SubscriptionManager
        mgr = SubscriptionManager()
        account_id = "demo_free_test_account_xyz789"
        for i in range(10):
            result = mgr.record_usage(account_id)
            assert result["allowed"] is True, f"Should be allowed on attempt {i+1}"
        # 11th attempt must be blocked
        result = mgr.record_usage(account_id)
        assert result["allowed"] is False
        assert result["remaining"] == 0

    def test_paid_users_are_unlimited(self):
        """Paid (non-free) users must never be blocked."""
        from src.subscription_manager import (
            SubscriptionManager, SubscriptionTier,
            SubscriptionRecord, SubscriptionStatus,
        )
        mgr = SubscriptionManager()
        account_id = "demo_paid_test_account_xyz789"
        mgr._subscriptions[account_id] = SubscriptionRecord(
            account_id=account_id,
            tier=SubscriptionTier.SOLO,
            status=SubscriptionStatus.ACTIVE,
        )
        for i in range(25):
            result = mgr.record_usage(account_id)
            assert result["allowed"] is True
            assert result["limit"] == -1  # unlimited

    def test_anon_usage_remaining_counts_down(self):
        """Remaining count must decrement with each use."""
        from src.subscription_manager import SubscriptionManager
        mgr = SubscriptionManager()
        fp = "demo_countdown_test_fp_444"
        result = mgr.record_anon_usage(fp)
        assert result["remaining"] == 4
        result = mgr.record_anon_usage(fp)
        assert result["remaining"] == 3
        result = mgr.record_anon_usage(fp)
        assert result["remaining"] == 2

