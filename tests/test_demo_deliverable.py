class TestDemoDeliverableEndpoint:
    """Tests for POST /api/demo/generate-deliverable."""

    def test_endpoint_exists(self, client):
        """Endpoint must exist and accept POST."""
        resp = _post_deliverable(client, "Onboard a new client")
        assert resp.status_code in (200, 429), f"Unexpected status {resp.status_code}"

    def test_missing_query_returns_400(self, client):
        """Empty query body returns 400."""
        resp = client.post(
            "/api/demo/generate-deliverable",
            content=json.dumps({}),
            headers={"Content-Type": "application/json"},
        )
        assert resp.status_code == 400
        assert resp.json()["error"] == "missing_query"

    def test_predefined_scenario_success(self, client):
        """A predefined scenario query returns a valid deliverable."""
        resp = _post_deliverable(client, "Generate Q3 finance report",
                                 headers={"X-Forwarded-For": "10.0.1.1"})
        if resp.status_code == 200:
            data = resp.json()
            assert data["success"] is True
            assert "deliverable" in data
            assert "title" in data["deliverable"]
            assert "content" in data["deliverable"]
            assert "filename" in data["deliverable"]
            assert data["deliverable"]["filename"].endswith(".txt")
        else:
            # 429 means limit exceeded from a previous test run — acceptable
            assert resp.status_code == 429

    def test_custom_query_success(self, client):
        """A custom query returns a deliverable."""
        resp = _post_deliverable(client, "Build a restaurant launch plan",
                                 headers={"X-Forwarded-For": "10.0.2.1"})
        if resp.status_code == 200:
            data = resp.json()
            assert data["success"] is True
            assert "deliverable" in data
            assert len(data["deliverable"]["content"]) > 100
        else:
            assert resp.status_code == 429

    def test_response_includes_usage_info(self, client):
        """Successful response includes usage stats."""
        resp = _post_deliverable(client, "Screen candidates for PM role",
                                 headers={"X-Forwarded-For": "10.0.3.1"})
        if resp.status_code == 200:
            data = resp.json()
            assert "usage" in data
            usage = data["usage"]
            assert "used" in usage
            assert "limit" in usage
            assert "remaining" in usage
            assert "tier" in usage

    def test_branding_in_response_content(self, client):
        """Returned content must contain Murphy branding."""
        resp = _post_deliverable(client, "Run compliance audit",
                                 headers={"X-Forwarded-For": "10.0.4.1"})
        if resp.status_code == 200:
            content = resp.json()["deliverable"]["content"]
            assert "murphy.systems" in content.lower() or "Murphy" in content

    def test_apache_license_in_content(self, client):
        """Returned content must contain Apache 1.0 license notice."""
        resp = _post_deliverable(client, "Create project plan",
                                 headers={"X-Forwarded-For": "10.0.5.1"})
        if resp.status_code == 200:
            content = resp.json()["deliverable"]["content"]
            assert "Apache License" in content

    def test_bsl_attribution_in_content(self, client):
        """Returned content must contain BSL 1.1 / Inoni attribution."""
        resp = _post_deliverable(client, "Process invoice batch",
                                 headers={"X-Forwarded-For": "10.0.6.1"})
        if resp.status_code == 200:
            content = resp.json()["deliverable"]["content"]
            assert "Inoni" in content
            assert "BSL" in content or "Business Source" in content


