def _post_deliverable(client, query, headers=None):
    """Helper: POST /api/demo/generate-deliverable with a query."""
    h = {"Content-Type": "application/json"}
    if headers:
        h.update(headers)
    return client.post(
        "/api/demo/generate-deliverable",
        content=json.dumps({"query": query}),
        headers=h,
    )


