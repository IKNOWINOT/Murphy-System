    # ------------------------------------------------------------------
    # Session / auth check
    # ------------------------------------------------------------------

    def check_terminal_access(self, user_id: str, terminal_path: str = "") -> Dict[str, Any]:
        """Return access status for a user trying to open a terminal.

        Returns::
            {"allowed": bool, "reason": str, "profile": dict | None}
        """
        try:
            profile = self.get_profile(user_id)
        except AuthError:
            return {"allowed": False, "reason": "user_not_found", "profile": None}

        if not profile.email_validated:
            return {
                "allowed": False,
                "reason": "email_not_validated",
                "profile": profile.to_dict(),
            }
        if not profile.eula_accepted:
            return {
                "allowed": False,
                "reason": "eula_not_accepted",
                "profile": profile.to_dict(),
            }

        # If a specific terminal is requested, validate role-based access
        if terminal_path:
            tc = self.assemble_terminal_config(user_id)
            if terminal_path not in tc.get("allowed_terminals", []):
                return {
                    "allowed": False,
                    "reason": "terminal_not_permitted_for_role",
                    "profile": profile.to_dict(),
                    "allowed_terminals": tc.get("allowed_terminals", []),
                }

        return {"allowed": True, "reason": "ok", "profile": profile.to_dict()}

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _audit(self, action: str, user_id: str, details: Dict[str, Any]) -> None:
        entry = {
            "action": action,
            "user_id": user_id,
            "timestamp": datetime.now(timezone.utc).isoformat(),
            **details,
        }
        capped_append(self._audit_log, entry, max_size=_MAX_AUDIT_LOG)

    def get_audit_log(self) -> List[Dict[str, Any]]:
        """Return a copy of the audit log."""
        with self._lock:
            return list(self._audit_log)
