    def assemble_terminal_config(self, user_id: str) -> Dict[str, Any]:
        """Assemble a terminal_config for the user based on their profile.

        Murphy infers which features to expose based on the user's position,
        department, role, and employee letter.  In production this would
        invoke an LLM planning pass; here we provide rule-based inference.
        """
        with self._lock:
            profile = self._profiles.get(user_id)
        if profile is None:
            raise AuthError("user not found")
        if not profile.is_fully_onboarded():
            raise AuthError("user is not fully onboarded")

        config: Dict[str, Any] = {
            "user_id": user_id,
            "role": profile.role,
            "features": {},
            "commands": [],
            "assembled_at": datetime.now(timezone.utc).isoformat(),
        }

        # Founder/admin gets full access
        if profile.role == "founder_admin":
            config["features"] = {
                "architect_terminal": True,
                "org_chart_editor": True,
                "user_management": True,
                "shadow_agent_config": True,
                "automation_library": True,
                "analytics_dashboard": True,
                "audit_log_viewer": True,
            }
            config["commands"] = ["*"]
        elif profile.role in ("admin", "org_admin"):
            config["features"] = {
                "architect_terminal": True,
                "org_chart_editor": True,
                "system_config": True,
                "compliance_dashboard": True,
                "full_audit_log": True,
            }
            config["terminal_path"] = "/ui/terminal-architect"
            config["commands"] = ["configure_system", "manage_org", "view_compliance", "audit"]
        elif profile.role in ("manager", "team_lead"):
            config["features"] = {
                "worker_terminal": True,
                "enhanced_terminal": True,
                "team_dashboard": True,
                "project_oversight": True,
                "hitl_review": True,
                "schedule_view": True,
                "automation_library": True,
                "analytics_dashboard": True,
                "shadow_agent_config": False,
            }
            config["terminal_path"] = "/ui/terminal-enhanced"
            config["commands"] = ["run", "status", "report", "assign",
                                   "manage_team", "review_outputs", "view_schedule", "approve"]
        elif profile.role == "employee":
            config["features"] = {
                "worker_terminal": True,
                "task_queue": True,
                "personal_deliverables": True,
                "chat_support": True,
            }
            config["terminal_path"] = "/ui/terminal-worker"
            config["commands"] = ["submit_work", "view_tasks", "chat"]
        elif profile.role in ("worker", "employee", "staff"):
            pos_lower = profile.position.lower()
            config["features"] = {
                "task_inbox": True,
                "deliverable_viewer": True,
                "time_tracker": True,
                "automation_library": "engineer" in pos_lower or "developer" in pos_lower,
                "analytics_dashboard": "analyst" in pos_lower,
            }
            config["terminal_path"] = "/ui/terminal-worker"
            config["commands"] = ["run", "submit_work", "view_tasks", "clock_in", "status"]
        elif profile.role in ("manager", "team_lead", "supervisor"):
            config["features"] = {
                "team_dashboard": True,
                "project_oversight": True,
                "approval_queue": True,
                "schedule_editor": True,
            }
            config["terminal_path"] = "/ui/terminal-enhanced"
            config["commands"] = ["approve", "assign", "review", "schedule"]
        elif profile.role in ("admin", "org_admin", "system_admin"):
            config["features"] = {
                "architect_terminal": True,
                "org_chart_editor": True,
                "system_config": True,
                "user_management": True,
                "compliance_dashboard": True,
            }
            config["terminal_path"] = "/ui/terminal-architect"
            config["commands"] = ["configure", "manage_users", "audit", "deploy"]
        else:
            # Fallback — infer from position text
            pos_lower = profile.position.lower()
            config["features"] = {
                "worker_terminal": True,
                "task_queue": True,
                "personal_deliverables": True,
                "chat_support": True,
                "automation_library": "engineer" in pos_lower or "developer" in pos_lower,
                "analytics_dashboard": "analyst" in pos_lower,
                "shadow_agent_config": False,
            }
            config["commands"] = ["run", "status", "submit_work", "view_tasks", "chat"]

        # Add recommended_terminal and allowed_terminals based on role
        if profile.role == "founder_admin":
            config["recommended_terminal"] = "/ui/terminal-unified"
            config["allowed_terminals"] = [
                "/ui/terminal-unified",
                "/ui/terminal-worker",
                "/ui/terminal-enhanced",
                "/ui/terminal-architect",
            ]
        elif profile.role in ("admin", "org_admin", "system_admin"):
            config["recommended_terminal"] = "/ui/terminal-architect"
            config["allowed_terminals"] = [
                "/ui/terminal-architect",
                "/ui/terminal-enhanced",
                "/ui/terminal-worker",
            ]
        elif profile.role in ("manager", "team_lead", "supervisor"):
            config["recommended_terminal"] = "/ui/terminal-enhanced"
            config["allowed_terminals"] = [
                "/ui/terminal-enhanced",
                "/ui/terminal-worker",
            ]
        elif profile.role in ("admin", "org_admin"):
            config["recommended_terminal"] = "/ui/terminal-architect"
            config["allowed_terminals"] = [
                "/ui/terminal-architect",
                "/ui/terminal-enhanced",
                "/ui/terminal-unified",
            ]
        elif profile.role in ("worker", "employee"):
            config["recommended_terminal"] = "/ui/terminal-worker"
            config["allowed_terminals"] = ["/ui/terminal-worker"]
        else:
            # Default — unified dashboard
            config["recommended_terminal"] = "/ui/terminal-unified"
            config["allowed_terminals"] = ["/ui/terminal-unified", "/ui/terminal-worker"]

        # Resolve terminal_path: honour user preference when it is allowed,
        # otherwise fall back to role default or recommended.
        if profile.preferred_terminal and profile.preferred_terminal in config["allowed_terminals"]:
            config["terminal_path"] = profile.preferred_terminal
        elif "terminal_path" not in config:
            config["terminal_path"] = config["recommended_terminal"]
        elif profile.role in ("worker", "employee", "staff"):
            config["recommended_terminal"] = "/ui/terminal-worker"
            config["allowed_terminals"] = ["/ui/terminal-worker"]
        else:
            # Legacy "manager" role kept for backwards-compat
            config["recommended_terminal"] = "/ui/terminal-enhanced"
            config["allowed_terminals"] = [
                "/ui/terminal-enhanced",
                "/ui/terminal-worker",
            ]

        # If the user has saved a preferred terminal, honour it when permitted
        if profile.preferred_terminal and profile.preferred_terminal in config.get("allowed_terminals", []):
            config["recommended_terminal"] = profile.preferred_terminal

        # Save back into profile
        with self._lock:
            self._profiles[user_id].terminal_config = config
            self._audit("assemble_terminal_config", user_id, {"role": profile.role})

        return config

