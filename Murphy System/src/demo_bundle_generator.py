def generate_demo_bundle(
    query: str,
    deliverable: Dict[str, Any],
    automation_spec: Dict[str, Any],
) -> Dict[str, Any]:
    """Generate a professional ZIP bundle containing all engagement documents.

    Args:
        query: The user's original request
        deliverable: Dict from generate_deliverable() with 'content', 'filename', 'title'
        automation_spec: Dict from generate_automation_spec() with full spec data

    Returns:
        dict with:
        - ``zip_bytes`` (bytes) — the ZIP file contents
        - ``filename`` (str) — suggested download filename
        - ``file_count`` (int) — number of files in the bundle
        - ``files`` (list[str]) — list of filenames in the bundle
    """
    spec_id = automation_spec.get("spec_id", "SPEC-PENDING")
    title = automation_spec.get("title", query[:60])

    # Build all documents
    proposal_txt = _build_automation_proposal(query, automation_spec, deliverable)
    quote_txt = _build_itemized_quote(query, automation_spec)
    readme_md = _build_bundle_readme(query, automation_spec)
    spec_txt = automation_spec.get("content_txt", "")
    deliverable_txt = deliverable.get("content", "")

    # Derive bundle folder name
    slug = re.sub(r"[^a-z0-9]+", "-", title.lower())[:40].strip("-")
    folder_name = f"murphy-{slug}-{spec_id.lower()}"
    zip_filename = f"{folder_name}.zip"

    # Build the ZIP
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        files_added: List[str] = []

        def _add(name: str, content: str) -> None:
            path = f"{folder_name}/{name}"
            zf.writestr(path, content)
            files_added.append(name)

        _add("README.md", readme_md)
        _add("deliverable.txt", deliverable_txt)
        _add("automation-proposal.txt", proposal_txt)
        _add("itemized-quote.txt", quote_txt)
        if spec_txt:
            _add("automation-spec.txt", spec_txt)
        _add("LICENSE", _LICENSE_NOTICE)

    zip_bytes = buf.getvalue()

    return {
        "zip_bytes": zip_bytes,
        "filename": zip_filename,
        "file_count": len(files_added),
        "files": files_added,
        "folder_name": folder_name,
        "spec_id": spec_id,
    }