        "",
        "─" * 72,
        "  SECTION B — PLATFORM SUBSCRIPTION (RECURRING)",
        "─" * 72,
        "",
        "  ┌───────────────────────────────┬────────────┬────────────┐",
        "  │ Platform Tier                 │ Monthly    │ Annual     │",
