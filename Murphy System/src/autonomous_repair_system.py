class TerminologyLockOnEngine:
    """Cross-module terminology consistency checker with Bayesian inference."""

    _KEY_TERMS = [
        "gate", "trigger", "schedule", "workflow", "agent",
        "task", "action", "event", "pipeline", "hook",
        "session", "context", "payload", "schema", "model",
    ]

    def __init__(self, src_root: str, boundary_controller: Any = None) -> None:
        self._src_root = src_root
        self._controller = boundary_controller
        self._concordance: Dict[str, TermConcordanceEntry] = {}
        self._lock = threading.Lock()

    def build_concordance_map(self) -> Dict[str, TermConcordanceEntry]:
        """Scan all source files and build the semantic concordance map."""
        concordance: Dict[str, TermConcordanceEntry] = {}

        for term in self._KEY_TERMS:
            concordance[term] = TermConcordanceEntry(term=term)

        if not os.path.isdir(self._src_root):
            with self._lock:
                self._concordance = concordance
            return concordance

        for root, _dirs, files in os.walk(self._src_root):
            if "__pycache__" in root:
                continue
            for fname in files:
                if not fname.endswith(".py"):
                    continue
                fpath = os.path.join(root, fname)
                rel = os.path.relpath(fpath, self._src_root)
                self._scan_file_for_terms(fpath, rel, concordance)

        for entry in concordance.values():
            entry.consistency_score = self._compute_consistency(entry)
            entry.bayesian_probability = self._bayesian_term_probability(entry)
            entry.flagged = entry.consistency_score < 0.7
            if entry.flagged:
                entry.alignment_proposals = [
                    f"Standardize usage of '{entry.term}' across modules: "
                    f"{list(entry.module_usages.keys())}"
                ]

        with self._lock:
            self._concordance = concordance

        return concordance

    def get_concordance_map(self) -> Dict[str, Any]:
        """Return the current concordance map as serializable dicts."""
        with self._lock:
            return {k: v.to_dict() for k, v in self._concordance.items()}

    def get_flagged_terms(self) -> List[TermConcordanceEntry]:
        """Return terms flagged for cross-module inconsistency."""
        with self._lock:
            return [v for v in self._concordance.values() if v.flagged]

    def _scan_file_for_terms(
        self,
        fpath: str,
        rel: str,
        concordance: Dict[str, TermConcordanceEntry],
    ) -> None:
        """Scan a single file for term usages."""
        try:
            with open(fpath, encoding="utf-8") as fh:
                content = fh.read().lower()
        except OSError as exc:
            logger.debug("Could not read %s: %s", fpath, exc)
            return

        for term, entry in concordance.items():
            pattern = re.compile(r"\b" + re.escape(term) + r"\b")
            matches = pattern.findall(content)
            if matches:
                if rel not in entry.module_usages:
                    entry.module_usages[rel] = []
                capped_append(entry.module_usages[rel], term, max_size=50)

    def _compute_consistency(self, entry: TermConcordanceEntry) -> float:
        """Compute a consistency score based on usage distribution."""
        n = len(entry.module_usages)
        if n == 0:
            return 1.0
        if n == 1:
            return 1.0
        counts = [len(v) for v in entry.module_usages.values()]
        total = sum(counts)
        if total == 0:
            return 1.0
        proportions = [c / total for c in counts]
        entropy = -sum(p * math.log(p + 1e-9) for p in proportions if p > 0)
        max_entropy = math.log(n + 1e-9)
        if max_entropy == 0.0:
            return 1.0
        normalized_entropy = entropy / max_entropy
        return max(0.0, 1.0 - normalized_entropy * 0.5)

    def _bayesian_term_probability(self, entry: TermConcordanceEntry) -> float:
        """Apply Bayesian update to estimate probability of consistent meaning."""
        prior = 0.9
        n = len(entry.module_usages)
        if n <= 1:
            return prior
        evidence_strength = min(n / 10.0, 1.0)
        likelihood_consistent = entry.consistency_score
        likelihood_inconsistent = 1.0 - entry.consistency_score
        posterior_numerator = likelihood_consistent * prior
        posterior_denominator = (
            likelihood_consistent * prior
            + likelihood_inconsistent * (1.0 - prior) * evidence_strength
        )
        if posterior_denominator == 0.0:
            return prior
        return posterior_numerator / posterior_denominator


