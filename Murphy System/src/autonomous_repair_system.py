class AutonomousRepairSystem:
    """Master autonomous repair orchestrator for the Murphy System.

    Design Label: ARCH-006
    Owner: Backend Team

    Combines multi-layer diagnosis, terminology lock-on, reconciliation loop,
    and immune system pattern into a single, safe orchestration layer.

    Safety invariants:
      - NEVER modifies source files on disk
      - All code changes are proposals for human review only
      - Bounded execution via max_iterations and timeout
      - Thread-safe: all shared state guarded by threading.Lock
      - Graceful degradation when sub-systems are unavailable
    """

    def __init__(
        self,
        src_root: Optional[str] = None,
        project_root: Optional[str] = None,
        bug_detector: Any = None,
        improvement_engine: Any = None,
        healing_coordinator: Any = None,
        event_backbone: Any = None,
        persistence_manager: Any = None,
        code_repair_engine: Any = None,
        boundary_controller: Any = None,
        desired_state: Optional[Dict[str, Any]] = None,
    ) -> None:
        self._src_root = src_root or "src"
        self._project_root = project_root or os.path.dirname(self._src_root)

        self._static_layer = StaticDiagnosisLayer(
            src_root=self._src_root,
            code_repair_engine=code_repair_engine,
        )
        self._runtime_layer = RuntimeDiagnosisLayer(
            bug_detector=bug_detector,
            improvement_engine=improvement_engine,
        )
        self._semantic_layer = SemanticDiagnosisLayer(
            boundary_controller=boundary_controller,
        )
        self._predictive_layer = PredictiveDiagnosisLayer()
        self._wiring_layer = WiringDiagnosisLayer(
            project_root=self._project_root,
        )
        self._terminology_engine = TerminologyLockOnEngine(
            src_root=self._src_root,
            boundary_controller=boundary_controller,
        )
        self._reconciliation_loop = ReconciliationLoop(
            src_root=self._src_root,
            desired_state=desired_state,
        )
        self._immune_system = ImmuneSystem(
            persistence_manager=persistence_manager,
        )

        self._backbone = event_backbone
        self._pm = persistence_manager
        self._healing_coordinator = healing_coordinator

        self._proposals: List[RepairProposal] = []
        self._reports: List[RepairReport] = []
        self._wiring_issues: List[WiringIssue] = []

        self._lock = threading.Lock()
        self._running = False
        self._status = RepairStatus.IDLE

    @property
    def status(self) -> RepairStatus:
        """Current status of the repair system."""
        with self._lock:
            return self._status

    def run_repair_cycle(
        self,
        max_iterations: int = _DEFAULT_MAX_ITERATIONS,
        timeout_seconds: float = _DEFAULT_TIMEOUT_SECONDS,
        layers: Optional[List[DiagnosisLayer]] = None,
    ) -> RepairReport:
        """Run a full autonomous repair cycle.

        Args:
            max_iterations: Maximum number of diagnosis-propose iterations.
            timeout_seconds: Wall-clock timeout for the entire cycle.
            layers: Specific layers to run, or None for all layers.

        Returns:
            A RepairReport summarising the cycle outcome.
        """
        with self._lock:
            if self._running:
                raise RuntimeError("Repair cycle already in progress")
            self._running = True
            self._status = RepairStatus.RUNNING

        start_time = time.monotonic()
        report = RepairReport(
            report_id=str(uuid.uuid4()),
            status=RepairStatus.RUNNING,
            iterations_run=0,
            proposals_generated=0,
            immune_memories_used=0,
            antibodies_generated=0,
            wiring_issues_found=0,
            drift_items_found=0,
            terminology_flags=0,
            predictions_made=0,
            duration_seconds=0.0,
            started_at=datetime.now(timezone.utc).isoformat(),
        )

        try:
            active_layers = layers or list(DiagnosisLayer)
            report.layers_run = [l.value for l in active_layers]

            self._publish_event("repair_cycle_started", {"report_id": report.report_id})

            all_proposals: List[RepairProposal] = []

            for iteration in range(max_iterations):
                elapsed = time.monotonic() - start_time
                if elapsed > timeout_seconds:
                    logger.warning(
                        "Repair cycle timed out after %.1fs (iteration %d)",
                        elapsed,
                        iteration,
                    )
                    break

                iteration_proposals = self._run_one_iteration(active_layers)
                all_proposals.extend(iteration_proposals)
                report.iterations_run = iteration + 1

                if not iteration_proposals:
                    break

            wiring_issues = self._run_wiring_validation()
            self._wiring_issues = wiring_issues
            report.wiring_issues_found = len(wiring_issues)

            concordance = self._terminology_engine.build_concordance_map()
            flagged = [v for v in concordance.values() if v.flagged]
            report.terminology_flags = len(flagged)

            recon_state = self._reconciliation_loop.reconcile()
            report.drift_items_found = len(recon_state.drift_items)

            immune_count, antibody_count = self._apply_immune_responses(all_proposals)
            report.immune_memories_used = immune_count
            report.antibodies_generated = antibody_count

            report.proposals_generated = len(all_proposals)
            with self._lock:
                for prop in all_proposals:
                    capped_append(self._proposals, prop, max_size=_MAX_PROPOSALS)

            elapsed_total = time.monotonic() - start_time
            report.duration_seconds = round(elapsed_total, 3)
            report.status = RepairStatus.COMPLETED
            report.completed_at = datetime.now(timezone.utc).isoformat()

            self._publish_event("repair_cycle_completed", report.to_dict())

        except Exception as exc:
            logger.error("Repair cycle failed: %s", exc)
            report.status = RepairStatus.FAILED
            report.completed_at = datetime.now(timezone.utc).isoformat()
            report.duration_seconds = round(time.monotonic() - start_time, 3)
        finally:
            with self._lock:
                self._running = False
                self._status = report.status
                capped_append(self._reports, report, max_size=_MAX_REPORTS)

        if self._pm is not None:
            try:
                self._pm.save(f"repair_report_{report.report_id}", report.to_dict())
            except Exception as exc:
                logger.debug("PersistenceManager save failed: %s", exc)

        return report

    def trigger_reconciliation(self) -> ReconciliationState:
        """Trigger a single reconciliation loop iteration."""
        return self._reconciliation_loop.reconcile()

    def get_wiring_report(self) -> List[Dict[str, Any]]:
        """Return the latest front-end to back-end wiring issues."""
        with self._lock:
            return [w.to_dict() for w in self._wiring_issues]

    def get_proposals(self) -> List[Dict[str, Any]]:
        """Return all generated repair proposals."""
        with self._lock:
            return [p.to_dict() for p in self._proposals]

    def get_reports(self) -> List[Dict[str, Any]]:
        """Return all repair cycle reports."""
        with self._lock:
            return [r.to_dict() for r in self._reports]

    def get_immune_memory(self) -> List[Dict[str, Any]]:
        """Return all immune memory cells."""
        return [c.to_dict() for c in self._immune_system.get_all_memory()]

    def get_terminology_concordance(self) -> Dict[str, Any]:
        """Return the current semantic concordance map."""
        return self._terminology_engine.get_concordance_map()

    def get_health(self) -> Dict[str, Any]:
        """Return extended system health information."""
        with self._lock:
            proposal_count = len(self._proposals)
            report_count = len(self._reports)
            last_report = self._reports[-1].to_dict() if self._reports else None

        return {
            "status": self.status.value,
            "proposals_generated": proposal_count,
            "reports_completed": report_count,
            "immune_memory_cells": len(self._immune_system.get_all_memory()),
            "last_report": last_report,
            "reconciliation_state": self._reconciliation_loop.get_state().to_dict(),
        }

    def _run_one_iteration(
        self,
        layers: List[DiagnosisLayer],
    ) -> List[RepairProposal]:
        """Run one iteration of multi-layer diagnosis and proposal generation."""
        proposals: List[RepairProposal] = []

        for layer in layers:
            diagnosis = self._run_layer(layer)
            layer_proposals = self._generate_proposals(diagnosis)
            proposals.extend(layer_proposals)

        return proposals

    def _run_layer(self, layer: DiagnosisLayer) -> DiagnosisResult:
        """Run a single diagnosis layer."""
        try:
            if layer == DiagnosisLayer.STATIC:
                return self._static_layer.run()
            if layer == DiagnosisLayer.RUNTIME:
                return self._runtime_layer.run()
            if layer == DiagnosisLayer.SEMANTIC:
                return self._semantic_layer.run()
            if layer == DiagnosisLayer.PREDICTIVE:
                return self._predictive_layer.run()
            if layer == DiagnosisLayer.WIRING:
                return self._wiring_layer.run()
        except Exception as exc:
            logger.debug("Layer %s failed: %s", layer.value, exc)

        return DiagnosisResult(
            result_id=str(uuid.uuid4()),
            layer=layer,
        )

    def _generate_proposals(self, diagnosis: DiagnosisResult) -> List[RepairProposal]:
        """Convert diagnosis issues into human-reviewable repair proposals."""
        proposals: List[RepairProposal] = []
        for issue in diagnosis.issues:
            proposal = RepairProposal(
                proposal_id=str(uuid.uuid4()),
                title=f"[{diagnosis.layer.value.upper()}] {issue.get('type', 'issue')}",
                description=issue.get("description", str(issue)),
                layer=diagnosis.layer,
                fix_steps=[
                    "Review the detected issue",
                    "Apply fix after human validation",
                ],
                affected_files=[issue.get("file", "")],
                confidence=0.75,
                requires_human_review=True,
            )
            proposals.append(proposal)
        return proposals

    def _run_wiring_validation(self) -> List[WiringIssue]:
        """Run full front-end to back-end wiring validation."""
        wiring_result = self._wiring_layer.run()
        issues: List[WiringIssue] = []
        for raw_issue in wiring_result.issues:
            kind_str = raw_issue.get("type", WiringIssueKind.MISSING_BACKEND_ENDPOINT.value)
            try:
                kind = WiringIssueKind(kind_str)
            except ValueError:
                kind = WiringIssueKind.MISSING_BACKEND_ENDPOINT
            wi = WiringIssue(
                issue_id=str(uuid.uuid4()),
                kind=kind,
                description=raw_issue.get("description", ""),
                frontend_ref=raw_issue.get("endpoint", ""),
                proposed_fix="Review endpoint contract and align frontend/backend",
            )
            capped_append(issues, wi, max_size=_MAX_WIRING_ISSUES)
        return issues

    def _apply_immune_responses(
        self,
        proposals: List[RepairProposal],
    ) -> Tuple[int, int]:
        """Apply immune system responses to known failure patterns."""
        immune_used = 0
        antibodies_generated = 0

        for proposal in proposals:
            response = self._immune_system.respond(proposal.description)
            if response is not None:
                if response.immunity_type == ImmunityType.ANTIBODY:
                    antibodies_generated += 1
                else:
                    immune_used += 1

        return immune_used, antibodies_generated

    def _publish_event(self, event_name: str, payload: Dict[str, Any]) -> None:
        """Publish an event to the EventBackbone if available."""
        try:
            from event_backbone_client import publish as _bb_publish  # noqa: PLC0415
            _bb_publish(
                "system_health",
                {"event_name": event_name, **payload},
                source="autonomous_repair_system",
                backbone=self._backbone,
            )
        except Exception as exc:
            logger.debug("EventBackbone publish failed: %s", exc)
