class ImmuneSystem:
    """Biological immune system pattern for adaptive failure recovery."""

    _INNATE_PATTERNS: Dict[str, Dict[str, Any]] = {
        "timeout": {
            "signature": "timed",
            "fix": "Increase timeout threshold or implement retry with backoff",
            "immunity_type": ImmunityType.INNATE,
        },
        "import_error": {
            "signature": "ImportError",
            "fix": "Verify dependency installation and PYTHONPATH configuration",
            "immunity_type": ImmunityType.INNATE,
        },
        "api_unreachable": {
            "signature": "ConnectionRefused",
            "fix": "Check service health and port binding configuration",
            "immunity_type": ImmunityType.INNATE,
        },
        "confidence_drift": {
            "signature": "confidence_below_threshold",
            "fix": "Trigger recalibration cycle via SelfImprovementEngine",
            "immunity_type": ImmunityType.INNATE,
        },
        "gate_bypass": {
            "signature": "gate_bypassed",
            "fix": "Re-enable gate enforcement and audit bypass logs",
            "immunity_type": ImmunityType.INNATE,
        },
    }

    def __init__(self, persistence_manager: Any = None) -> None:
        self._pm = persistence_manager
        self._memory: Dict[str, ImmuneMemoryCell] = {}
        self._lock = threading.Lock()
        self._load_innate_immunity()

    def _load_innate_immunity(self) -> None:
        """Pre-load innate immunity patterns into memory."""
        for key, pattern in self._INNATE_PATTERNS.items():
            cell = ImmuneMemoryCell(
                cell_id=key,
                error_signature=pattern["signature"],
                failure_description=f"Innate immunity: {key}",
                fix_applied=pattern["fix"],
                immunity_type=pattern["immunity_type"],
            )
            self._memory[key] = cell

    def respond(self, error_description: str) -> Optional[ImmuneMemoryCell]:
        """Attempt to find an immune response for the given error."""
        with self._lock:
            for cell in self._memory.values():
                if cell.error_signature.lower() in error_description.lower():
                    cell.success_count += 1
                    cell.last_used = datetime.now(timezone.utc).isoformat()
                    return cell

        antibody = self._generate_antibody(error_description)
        return antibody

    def memorize_fix(
        self,
        error_description: str,
        fix_applied: str,
        signature: Optional[str] = None,
    ) -> ImmuneMemoryCell:
        """Memorize a new fix as an adaptive immunity pattern."""
        cell_id = str(uuid.uuid4())
        error_sig = signature or self._extract_signature(error_description)
        cell = ImmuneMemoryCell(
            cell_id=cell_id,
            error_signature=error_sig,
            failure_description=error_description,
            fix_applied=fix_applied,
            immunity_type=ImmunityType.ADAPTIVE,
        )
        with self._lock:
            self._memory[cell_id] = cell
            if len(self._memory) > _MAX_IMMUNE_MEMORY:
                oldest_key = next(iter(self._memory))
                del self._memory[oldest_key]

        if self._pm is not None:
            try:
                self._pm.save(f"immune_memory_{cell_id}", cell.to_dict())
            except Exception as exc:
                logger.debug("PersistenceManager immune save failed: %s", exc)

        return cell

    def get_all_memory(self) -> List[ImmuneMemoryCell]:
        """Return all immune memory cells."""
        with self._lock:
            return list(self._memory.values())

    def _generate_antibody(self, error_description: str) -> Optional[ImmuneMemoryCell]:
        """Generate a variant fix (antibody) for a similar-but-novel failure."""
        with self._lock:
            best_cell: Optional[ImmuneMemoryCell] = None
            best_score = 0.0
            for cell in self._memory.values():
                score = self._similarity(error_description, cell.failure_description)
                if score > best_score:
                    best_score = score
                    best_cell = cell

        if best_cell is None or best_score < 0.3:
            return None

        antibody = ImmuneMemoryCell(
            cell_id=str(uuid.uuid4()),
            error_signature=self._extract_signature(error_description),
            failure_description=error_description,
            fix_applied=f"[Antibody variant of '{best_cell.cell_id}'] {best_cell.fix_applied}",
            immunity_type=ImmunityType.ANTIBODY,
        )
        return antibody

    def _extract_signature(self, description: str) -> str:
        """Extract a compact signature from an error description."""
        words = re.findall(r"\b[A-Z][a-zA-Z]+Error\b|\btimeout\b|\bfailed\b", description)
        if words:
            return words[0]
        tokens = description.split()
        return " ".join(tokens[:3]) if len(tokens) >= 3 else description[:50]

    def _similarity(self, a: str, b: str) -> float:
        """Compute a simple token overlap similarity between two strings."""
        tokens_a = set(a.lower().split())
        tokens_b = set(b.lower().split())
        intersection = tokens_a & tokens_b
        union = tokens_a | tokens_b
        if not union:
            return 0.0
        return len(intersection) / (len(union) or 1)


