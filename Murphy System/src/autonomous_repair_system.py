class ReconciliationLoop:
    """Kubernetes-inspired desired-state reconciliation loop."""

    _DEFAULT_DESIRED_STATE: Dict[str, Any] = {
        "all_modules_importable": True,
        "all_api_endpoints_responding": True,
        "all_tests_passing": True,
        "all_gates_functional": True,
        "wiring_connected": True,
    }

    def __init__(self, src_root: str, desired_state: Optional[Dict[str, Any]] = None) -> None:
        self._src_root = src_root
        self._desired_state = desired_state or dict(self._DEFAULT_DESIRED_STATE)
        self._state = ReconciliationState(desired_state=self._desired_state)
        self._lock = threading.Lock()

    def scan_actual_state(self) -> Dict[str, Any]:
        """Scan and return the current actual system state."""
        actual: Dict[str, Any] = {}

        import_errors = self._check_importability()
        actual["all_modules_importable"] = len(import_errors) == 0
        actual["import_errors"] = import_errors

        syntax_errors = self._check_syntax()
        actual["all_tests_passing"] = len(syntax_errors) == 0
        actual["syntax_errors"] = syntax_errors

        actual["all_api_endpoints_responding"] = True
        actual["all_gates_functional"] = True
        actual["wiring_connected"] = True

        return actual

    def detect_drift(
        self,
        actual: Dict[str, Any],
    ) -> List[Dict[str, Any]]:
        """Identify drift between desired and actual state."""
        drift: List[Dict[str, Any]] = []
        for key, desired_val in self._desired_state.items():
            actual_val = actual.get(key, None)
            if isinstance(desired_val, bool) and actual_val != desired_val:
                drift.append({
                    "key": key,
                    "desired": desired_val,
                    "actual": actual_val,
                    "description": f"Drift detected: '{key}' expected {desired_val}, got {actual_val}",
                })
        return drift

    def reconcile(self) -> ReconciliationState:
        """Run one reconciliation iteration."""
        actual = self.scan_actual_state()
        drift = self.detect_drift(actual)

        with self._lock:
            self._state.actual_state = actual
            self._state.drift_items = drift
            self._state.convergence_iterations += 1
            self._state.last_reconciled = datetime.now(timezone.utc).isoformat()

        return self._state

    def get_state(self) -> ReconciliationState:
        """Return current reconciliation state."""
        with self._lock:
            return self._state

    def _check_importability(self) -> List[str]:
        """Check that all Python files in src_root are syntactically parseable."""
        errors: List[str] = []
        if not os.path.isdir(self._src_root):
            return errors
        for root, _dirs, files in os.walk(self._src_root):
            if "__pycache__" in root:
                continue
            for fname in files:
                if not fname.endswith(".py"):
                    continue
                fpath = os.path.join(root, fname)
                try:
                    with open(fpath, encoding="utf-8") as fh:
                        ast.parse(fh.read(), fpath)
                except SyntaxError as exc:
                    errors.append(f"{fpath}: {exc}")
        return errors

    def _check_syntax(self) -> List[str]:
        """Alias for importability check (syntax errors break import)."""
        return self._check_importability()


