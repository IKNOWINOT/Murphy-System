class StaticDiagnosisLayer:
    """Static analysis layer using AST inspection and import graph validation."""

    def __init__(self, src_root: str, code_repair_engine: Any = None) -> None:
        self._src_root = src_root
        self._repair_engine = code_repair_engine

    def run(self) -> DiagnosisResult:
        """Run static analysis and return issues."""
        result = DiagnosisResult(
            result_id=str(uuid.uuid4()),
            layer=DiagnosisLayer.STATIC,
        )
        issues: List[Dict[str, Any]] = []

        if self._repair_engine is not None:
            try:
                if os.path.isdir(self._src_root):
                    for root, _dirs, files in os.walk(self._src_root):
                        for fname in files:
                            if not fname.endswith(".py"):
                                continue
                            fpath = os.path.join(root, fname)
                            file_issues = self._repair_engine.analyze_file(fpath)
                            for issue in (file_issues or []):
                                issues.append({
                                    "type": "static",
                                    "file": fpath,
                                    "issue": str(issue),
                                })
            except Exception as exc:
                logger.debug("CodeRepairEngine static analysis failed: %s", exc)

        issues.extend(self._check_import_graph())
        result.issues = issues
        result.severity = "high" if issues else "low"
        return result

    def _check_import_graph(self) -> List[Dict[str, Any]]:
        """Validate Python import graph for obvious errors."""
        issues: List[Dict[str, Any]] = []
        if not os.path.isdir(self._src_root):
            return issues
        for root, _dirs, files in os.walk(self._src_root):
            if "__pycache__" in root:
                continue
            for fname in files:
                if not fname.endswith(".py"):
                    continue
                fpath = os.path.join(root, fname)
                try:
                    with open(fpath, encoding="utf-8") as fh:
                        ast.parse(fh.read(), fpath)
                except SyntaxError as exc:
                    issues.append({
                        "type": "syntax_error",
                        "file": fpath,
                        "issue": str(exc),
                    })
        return issues


class RuntimeDiagnosisLayer:
    """Runtime analysis layer using BugPatternDetector and SelfImprovementEngine."""

    def __init__(
        self,
        bug_detector: Any = None,
        improvement_engine: Any = None,
    ) -> None:
        self._detector = bug_detector
        self._engine = improvement_engine

    def run(self) -> DiagnosisResult:
        """Run runtime diagnosis and return issues."""
        result = DiagnosisResult(
            result_id=str(uuid.uuid4()),
            layer=DiagnosisLayer.RUNTIME,
        )
        issues: List[Dict[str, Any]] = []

        if self._detector is not None:
            try:
                self._detector.run_detection_cycle()
                for pattern in self._detector.get_patterns(limit=50):
                    issues.append({
                        "type": "bug_pattern",
                        "pattern_id": pattern.get("pattern_id", ""),
                        "description": pattern.get("representative_message", ""),
                        "severity": pattern.get("severity", "medium"),
                    })
            except Exception as exc:
                logger.debug("BugPatternDetector runtime analysis failed: %s", exc)

        if self._engine is not None:
            try:
                for proposal in self._engine.get_remediation_backlog():
                    issues.append({
                        "type": "remediation_needed",
                        "proposal_id": proposal.proposal_id,
                        "description": proposal.description,
                        "priority": proposal.priority,
                    })
            except Exception as exc:
                logger.debug("SelfImprovementEngine runtime analysis failed: %s", exc)

        result.issues = issues
        result.severity = "high" if issues else "low"
        return result


class SemanticDiagnosisLayer:
    """Semantic analysis layer using SemanticsBoundaryController."""

    def __init__(self, boundary_controller: Any = None) -> None:
        self._controller = boundary_controller

    def run(self) -> DiagnosisResult:
        """Run semantic diagnosis for belief-state and boundary violations."""
        result = DiagnosisResult(
            result_id=str(uuid.uuid4()),
            layer=DiagnosisLayer.SEMANTIC,
        )
        issues: List[Dict[str, Any]] = []

        if self._controller is not None:
            try:
                hypotheses = self._controller.get_all_hypotheses()
                for hyp in (hypotheses or []):
                    posterior = hyp.get("posterior", 1.0)
                    if posterior < 0.3:
                        issues.append({
                            "type": "low_confidence_hypothesis",
                            "hypothesis_id": hyp.get("hypothesis_id", ""),
                            "description": hyp.get("description", ""),
                            "posterior": posterior,
                        })
            except Exception as exc:
                logger.debug("SemanticsBoundaryController semantic analysis failed: %s", exc)

        result.issues = issues
        result.severity = "medium" if issues else "low"
        return result


class PredictiveDiagnosisLayer:
    """Predictive analysis layer using time-series error rate analysis."""

    def __init__(self) -> None:
        self._error_history: List[Tuple[float, int]] = []
        self._lock = threading.Lock()

    def record_error_count(self, count: int) -> None:
        """Record an observed error count at the current time."""
        with self._lock:
            capped_append(
                self._error_history,
                (time.monotonic(), count),
                max_size=200,
            )

    def run(self) -> DiagnosisResult:
        """Run predictive analysis and return risk predictions."""
        result = DiagnosisResult(
            result_id=str(uuid.uuid4()),
            layer=DiagnosisLayer.PREDICTIVE,
        )
        issues: List[Dict[str, Any]] = []

        with self._lock:
            history = list(self._error_history)

        if len(history) >= 3:
            trend = self._compute_trend(history)
            if trend > 0.5:
                issues.append({
                    "type": "rising_error_trend",
                    "description": "Error rate is increasing — predicted failure imminent",
                    "trend_slope": round(trend, 4),
                    "data_points": len(history),
                })

        result.issues = issues
        result.severity = "high" if issues else "low"
        return result

    def _compute_trend(self, history: List[Tuple[float, int]]) -> float:
        """Compute linear regression slope over error counts."""
        n = len(history)
        if n < 2:
            return 0.0
        xs = [h[0] for h in history]
        ys = [float(h[1]) for h in history]
        x_mean = sum(xs) / n
        y_mean = sum(ys) / n
        numerator = sum((xs[i] - x_mean) * (ys[i] - y_mean) for i in range(n))
        denominator = sum((xs[i] - x_mean) ** 2 for i in range(n))
        if denominator == 0.0:
            return 0.0
        return numerator / denominator


class WiringDiagnosisLayer:
    """Front-end to back-end wiring validation layer."""

    _FRONTEND_FILES = [
        "onboarding_wizard.html",
        "murphy_overlay.js",
        "murphy_auth.js",
    ]
    _BACKEND_FLASK_FILES = [
        "bots/rest_api.py",
        "src/module_compiler/api/endpoints.py",
        "src/compute_plane/api/endpoints.py",
        "src/execution_orchestrator/api.py",
    ]
    _KNOWN_PORTS = {8053, 8054, 5000, 8000, 8080}
    _AUTHORIZED_BACKEND_PORTS = {8053, 8054}

    def __init__(self, project_root: str) -> None:
        self._project_root = project_root

    def run(self) -> DiagnosisResult:
        """Validate front-end to back-end wiring."""
        result = DiagnosisResult(
            result_id=str(uuid.uuid4()),
            layer=DiagnosisLayer.WIRING,
        )
        issues: List[Dict[str, Any]] = []

        frontend_endpoints = self._extract_frontend_endpoints()
        backend_endpoints = self._extract_backend_endpoints()

        for ep in frontend_endpoints:
            if ep not in backend_endpoints:
                issues.append({
                    "type": WiringIssueKind.MISSING_BACKEND_ENDPOINT.value,
                    "description": f"Frontend calls '{ep}' but no backend route found",
                    "endpoint": ep,
                })

        port_issues = self._detect_port_mismatches_in_files()
        issues.extend(port_issues)

        result.issues = issues
        result.severity = "high" if issues else "low"
        return result

    def _extract_frontend_endpoints(self) -> List[str]:
        """Extract API endpoint paths called by frontend files."""
        endpoints: List[str] = []
        url_pattern = re.compile(r"""['"`](/api/[^'"`\s]+)['"`]""")
        for relative in self._FRONTEND_FILES:
            fpath = os.path.join(self._project_root, relative)
            if not os.path.isfile(fpath):
                continue
            try:
                with open(fpath, encoding="utf-8") as fh:
                    content = fh.read()
                for match in url_pattern.finditer(content):
                    endpoints.append(match.group(1))
            except OSError as exc:
                logger.debug("Could not read frontend file %s: %s", fpath, exc)
        return list(set(endpoints))

    def _extract_backend_endpoints(self) -> List[str]:
        """Extract registered Flask route paths from backend files."""
        endpoints: List[str] = []
        route_pattern = re.compile(r"""@\w+\.route\(['"`]([^'"`]+)['"`]""")
        for relative in self._BACKEND_FLASK_FILES:
            fpath = os.path.join(self._project_root, relative)
            if not os.path.isfile(fpath):
                continue
            try:
                with open(fpath, encoding="utf-8") as fh:
                    content = fh.read()
                for match in route_pattern.finditer(content):
                    endpoints.append(match.group(1))
            except OSError as exc:
                logger.debug("Could not read backend file %s: %s", fpath, exc)
        return list(set(endpoints))

    def _detect_port_mismatches_in_files(self) -> List[Dict[str, Any]]:
        """Detect port mismatches by scanning raw frontend file content."""
        issues: List[Dict[str, Any]] = []
        full_url_re = re.compile(r"""https?://[^'"`\s]*:(\d+)(/api/[^'"`\s]*)""")
        for relative in self._FRONTEND_FILES:
            fpath = os.path.join(self._project_root, relative)
            if not os.path.isfile(fpath):
                continue
            try:
                with open(fpath, encoding="utf-8") as fh:
                    content = fh.read()
                for match in full_url_re.finditer(content):
                    port = int(match.group(1))
                    path = match.group(2)
                    if port not in self._AUTHORIZED_BACKEND_PORTS:
                        issues.append({
                            "type": WiringIssueKind.PORT_MISMATCH.value,
                            "description": (
                                f"Frontend uses port {port} for '{path}', "
                                f"expected one of {sorted(self._AUTHORIZED_BACKEND_PORTS)}"
                            ),
                            "endpoint": path,
                            "detected_port": port,
                        })
            except OSError as exc:
                logger.debug("Could not read frontend file %s: %s", fpath, exc)
        return issues


