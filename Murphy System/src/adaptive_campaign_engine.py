@dataclass
class TierPerformanceSnapshot:
    """Point-in-time performance metrics for a tier campaign."""
    period: str                    # ISO date or label, e.g. "2026-W09"
    impressions: int = 0
    leads: int = 0
    conversions: int = 0
    spend_usd: float = 0.0
    recorded_at: str = field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )

    @property
    def conversion_rate(self) -> float:
        """Leads → conversions rate.  Returns 0 if no leads."""
        return self.conversions / self.leads if self.leads > 0 else 0.0

    @property
    def lead_rate(self) -> float:
        """Impressions → leads rate.  Returns 0 if no impressions."""
        return self.leads / self.impressions if self.impressions > 0 else 0.0

    def to_dict(self) -> Dict[str, Any]:
        return {
            "period": self.period,
            "impressions": self.impressions,
            "leads": self.leads,
            "conversions": self.conversions,
            "spend_usd": round(self.spend_usd, 2),
            "conversion_rate": round(self.conversion_rate, 4),
            "lead_rate": round(self.lead_rate, 4),
            "recorded_at": self.recorded_at,
        }


