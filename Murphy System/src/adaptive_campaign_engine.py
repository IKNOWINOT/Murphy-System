@dataclass
class TierCampaign:
    """Campaign state for a single pricing tier."""
    tier: str
    status: TierCampaignStatus = TierCampaignStatus.ACTIVE
    channels: List[str] = field(default_factory=list)
    demographics: List[str] = field(default_factory=list)
    messaging: str = ""
    monthly_revenue_target: float = 0.0
    snapshots: List[TierPerformanceSnapshot] = field(default_factory=list)
    adjustments: List[CampaignAdjustment] = field(default_factory=list)
    proposals: List[PaidAdProposal] = field(default_factory=list)
    created_at: str = field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )

    @property
    def total_conversions(self) -> int:
        return sum(s.conversions for s in self.snapshots)

    @property
    def total_leads(self) -> int:
        return sum(s.leads for s in self.snapshots)

    @property
    def total_spend(self) -> float:
        return sum(s.spend_usd for s in self.snapshots)

    def latest_snapshot(self) -> Optional[TierPerformanceSnapshot]:
        return self.snapshots[-1] if self.snapshots else None

    def to_dict(self) -> Dict[str, Any]:
        return {
            "tier": self.tier,
            "status": self.status.value,
            "channels": list(self.channels),
            "demographics": list(self.demographics),
            "messaging": self.messaging,
            "monthly_revenue_target": round(self.monthly_revenue_target, 2),
            "total_conversions": self.total_conversions,
            "total_leads": self.total_leads,
            "total_spend": round(self.total_spend, 2),
            "adjustment_count": len(self.adjustments),
            "proposal_count": len(self.proposals),
            "snapshot_count": len(self.snapshots),
        }


