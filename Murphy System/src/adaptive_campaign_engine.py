# ---------------------------------------------------------------------------
# Adaptive Campaign Engine
# ---------------------------------------------------------------------------

class AdaptiveCampaignEngine:
    """Manages per-tier marketing campaigns with automatic traction adjustment.

    Responsibilities:
      1. Bootstrap one campaign per pricing tier
      2. Ingest performance snapshots each evaluation period
      3. Detect low traction and auto-adjust channels / demographics
      4. Propose paid-advertising campaigns when organic pivots fail
      5. Gate all paid spend behind founder HITL approval
      6. Publish events for every adjustment and proposal
    """

    # Traction thresholds (conversion rate: leads → paying)
    HEALTHY_CONVERSION_RATE = 0.03    # ≥3% = healthy
    LOW_CONVERSION_RATE = 0.01        # 1-3% = low
    # Below 1% = critical

    # How many consecutive low-traction periods before we pivot
    ADJUSTMENT_TRIGGER_PERIODS = 2
    # How many adjustments before proposing paid ads
    MAX_ORGANIC_ADJUSTMENTS = 3

    def __init__(self) -> None:
        self._campaigns: Dict[str, TierCampaign] = {}
        self._lock = threading.Lock()
        self._event_log: List[Dict[str, Any]] = []

    # ------------------------------------------------------------------
    # Bootstrap
    # ------------------------------------------------------------------

    def bootstrap_tier_campaigns(
        self,
        tiers: Optional[List[str]] = None,
    ) -> Dict[str, TierCampaign]:
        """Create one campaign per tier using default configurations."""
        tiers = tiers or list(_DEFAULT_TIER_CAMPAIGNS.keys())
        with self._lock:
            for tier in tiers:
                if tier in self._campaigns:
                    continue
                cfg = _DEFAULT_TIER_CAMPAIGNS.get(tier, {})
                tc = TierCampaign(
                    tier=tier,
                    channels=list(cfg.get("channels", [])),
                    demographics=list(cfg.get("demographics", [])),
                    messaging=cfg.get("messaging", ""),
                )
                self._campaigns[tier] = tc
                self._log_event("campaign_bootstrapped", tier, {
                    "channels": tc.channels,
                    "demographics": tc.demographics,
                })
        return dict(self._campaigns)

    # ------------------------------------------------------------------
    # Performance ingestion
    # ------------------------------------------------------------------

    def record_snapshot(
        self,
        tier: str,
        period: str,
        impressions: int = 0,
        leads: int = 0,
        conversions: int = 0,
        spend_usd: float = 0.0,
    ) -> Optional[TierPerformanceSnapshot]:
        """Record a performance snapshot for a tier campaign."""
        with self._lock:
            tc = self._campaigns.get(tier)
            if tc is None:
                return None
            snap = TierPerformanceSnapshot(
                period=period,
                impressions=impressions,
                leads=leads,
                conversions=conversions,
                spend_usd=spend_usd,
            )
            capped_append(tc.snapshots, snap, max_size=1_000)
            return snap

    # ------------------------------------------------------------------
    # Traction evaluation
    # ------------------------------------------------------------------

    def evaluate_traction(self, tier: str) -> Optional[Dict[str, Any]]:
        """Evaluate traction for a tier and return status + recommended action."""
        with self._lock:
            tc = self._campaigns.get(tier)
            if tc is None:
                return None
            recent = tc.snapshots[-self.ADJUSTMENT_TRIGGER_PERIODS:] if tc.snapshots else []

        if not recent:
            return {
                "tier": tier,
                "traction": TractionLevel.HEALTHY.value,
                "action": "awaiting_data",
                "message": "No performance data yet",
            }

        avg_conv_rate = (
            sum(s.conversion_rate for s in recent) / (len(recent) or 1)
        )

        if avg_conv_rate >= self.HEALTHY_CONVERSION_RATE:
            traction = TractionLevel.HEALTHY
            action = "maintain"
            message = f"Conversion rate {avg_conv_rate:.2%} is healthy"
        elif avg_conv_rate >= self.LOW_CONVERSION_RATE:
            traction = TractionLevel.LOW
            action = "adjust_campaign"
            message = (
                f"Conversion rate {avg_conv_rate:.2%} is low — "
                "recommend channel/demographic adjustment"
            )
        else:
            traction = TractionLevel.CRITICAL
            organic_adjustments = len(tc.adjustments)
            if organic_adjustments >= self.MAX_ORGANIC_ADJUSTMENTS:
                action = "propose_paid_ads"
                message = (
                    f"Conversion rate {avg_conv_rate:.2%} is critical after "
                    f"{organic_adjustments} organic adjustments — "
                    "recommend paid advertising proposal"
                )
            else:
                action = "adjust_campaign"
                message = (
                    f"Conversion rate {avg_conv_rate:.2%} is critical — "
                    "recommend aggressive channel/demographic pivot"
                )

        return {
            "tier": tier,
            "traction": traction.value,
            "avg_conversion_rate": round(avg_conv_rate, 4),
            "action": action,
            "message": message,
            "recent_periods": len(recent),
            "total_adjustments": len(tc.adjustments),
        }

    # ------------------------------------------------------------------
    # Auto-adjust campaigns
    # ------------------------------------------------------------------

    def adjust_campaign(self, tier: str) -> Optional[CampaignAdjustment]:
        """Automatically pivot channels and demographics for a tier.

        Rotates in alternate channels/demographics from the pivot pool
        while preserving any channels that are still performing.
        """
        with self._lock:
            tc = self._campaigns.get(tier)
            if tc is None:
                return None

            pivot = _PIVOT_OPTIONS.get(tier, {})
            old_channels = list(tc.channels)
            old_demographics = list(tc.demographics)
            old_messaging = tc.messaging

            # Rotate: swap in pivot options, keep 1 original channel
            new_channels = pivot.get("channels", old_channels)
            if old_channels:
                # Keep first original channel for continuity
                new_channels = [old_channels[0]] + [
                    c for c in new_channels if c != old_channels[0]
                ][:3]

            new_demographics = pivot.get("demographics", old_demographics)

            # Build adjusted messaging
            default_cfg = _DEFAULT_TIER_CAMPAIGNS.get(tier, {})
            new_messaging = default_cfg.get("messaging", old_messaging)
            if len(tc.adjustments) > 0:
                new_messaging = f"[Adjusted v{len(tc.adjustments) + 1}] {new_messaging}"

            adjustment = CampaignAdjustment(
                adjustment_id=f"adj-{uuid.uuid4().hex[:8]}",
                tier=tier,
                reason="Low traction detected — pivoting channels and demographics",
                old_channels=old_channels,
                new_channels=new_channels,
                old_demographics=old_demographics,
                new_demographics=new_demographics,
                old_messaging=old_messaging,
                new_messaging=new_messaging,
            )

            tc.channels = new_channels
            tc.demographics = new_demographics
            tc.messaging = new_messaging
            tc.status = TierCampaignStatus.ADJUSTING
            capped_append(tc.adjustments, adjustment, max_size=100)

            self._log_event("campaign_adjusted", tier, adjustment.to_dict())
            return adjustment

    # ------------------------------------------------------------------
    # Paid advertising proposals (HITL-gated)
    # ------------------------------------------------------------------

    def propose_paid_campaign(
        self,
        tier: str,
        proposed_budget_usd: float,
    ) -> Optional[PaidAdProposal]:
        """Generate a paid-advertising proposal requiring founder approval.

        The system builds a market strategy rationale based on current
        performance data and projects ROI from the tier's economics.
        """
        with self._lock:
            tc = self._campaigns.get(tier)
            if tc is None:
                return None

            # Build strategy from performance data
            total_imp = sum(s.impressions for s in tc.snapshots)
            total_leads = tc.total_leads
            total_conv = tc.total_conversions
            adjustments_tried = len(tc.adjustments)

            conv_rate = total_conv / total_leads if total_leads > 0 else 0.0

            # Select paid channels based on tier
            paid_channels = self._select_paid_channels(tier)
            target_demos = self._select_paid_demographics(tier)

            # Project ROI: conservative estimate based on improved reach
            projected_new_leads = int(proposed_budget_usd * 0.5)  # ~$2/lead
            projected_conversions = max(1, int(projected_new_leads * max(conv_rate, 0.02)))

            # Revenue lookup per tier (single source of truth)
            revenue_per_conversion = _DEFAULT_TIER_REVENUES.get(tier, 50.0) * 12  # Annual
            projected_revenue = projected_conversions * revenue_per_conversion
            projected_roi = (
                projected_revenue / proposed_budget_usd
                if proposed_budget_usd > 0 else 0.0
            )

            rationale = (
                f"Tier '{tier}' has had {adjustments_tried} organic adjustments "
                f"with {total_imp:,} impressions, {total_leads:,} leads, "
                f"and {total_conv:,} conversions ({conv_rate:.2%} rate). "
                f"Organic channels exhausted — paid advertising recommended "
                f"to accelerate tier fill."
            )

            market_strategy = (
                f"STRATEGY: Allocate ${proposed_budget_usd:,.0f} across "
                f"{', '.join(paid_channels)} targeting {', '.join(target_demos)}. "
                f"Projected {projected_new_leads:,} new leads → "
                f"{projected_conversions:,} conversions → "
                f"${projected_revenue:,.0f} annual revenue "
                f"({projected_roi:.1f}x ROI). "
                f"Based on current funnel data and {adjustments_tried} failed "
                f"organic pivots. Requires founder approval before execution."
            )

            proposal = PaidAdProposal(
                proposal_id=f"paid-{uuid.uuid4().hex[:8]}",
                tier=tier,
                proposed_budget_usd=proposed_budget_usd,
                proposed_channels=paid_channels,
                target_demographics=target_demos,
                rationale=rationale,
                projected_roi=projected_roi,
                market_strategy=market_strategy,
            )

            capped_append(tc.proposals, proposal, max_size=50)
            tc.status = TierCampaignStatus.PENDING_APPROVAL

            self._log_event("paid_ad_proposed", tier, {
                "proposal_id": proposal.proposal_id,
                "budget": proposed_budget_usd,
                "projected_roi": projected_roi,
                "requires": "founder_hitl_approval",
            })

            return proposal

    def approve_proposal(
        self,
        tier: str,
        proposal_id: str,
        approved_by: str,
    ) -> Optional[PaidAdProposal]:
        """Founder approves a paid-ad proposal (HITL gate)."""
        with self._lock:
            tc = self._campaigns.get(tier)
            if tc is None:
                return None
            for p in tc.proposals:
                if p.proposal_id == proposal_id and p.status == ProposalStatus.PENDING:
                    p.status = ProposalStatus.APPROVED
                    p.approved_by = approved_by
                    p.approved_at = datetime.now(timezone.utc).isoformat()
                    tc.status = TierCampaignStatus.ACTIVE
                    self._log_event("paid_ad_approved", tier, {
                        "proposal_id": proposal_id,
                        "approved_by": approved_by,
                    })
                    return p
        return None

    def reject_proposal(
        self,
        tier: str,
        proposal_id: str,
        reason: str = "",
    ) -> Optional[PaidAdProposal]:
        """Founder rejects a paid-ad proposal."""
        with self._lock:
            tc = self._campaigns.get(tier)
            if tc is None:
                return None
            for p in tc.proposals:
                if p.proposal_id == proposal_id and p.status == ProposalStatus.PENDING:
                    p.status = ProposalStatus.REJECTED
                    p.rejection_reason = reason
                    tc.status = TierCampaignStatus.ACTIVE
                    self._log_event("paid_ad_rejected", tier, {
                        "proposal_id": proposal_id,
                        "reason": reason,
                    })
                    return p
        return None

    # ------------------------------------------------------------------
    # Evaluate all tiers and take action
    # ------------------------------------------------------------------

    def evaluate_and_act(self) -> Dict[str, Any]:
        """Evaluate all tier campaigns and take automatic action.

        For each tier:
          - If healthy: maintain
          - If low/critical with adjustments left: auto-adjust
          - If critical after max adjustments: propose paid ads

        Returns a summary of all actions taken.
        """
        results: Dict[str, Any] = {}
        tiers = list(self._campaigns.keys())

        for tier in tiers:
            eval_result = self.evaluate_traction(tier)
            if eval_result is None:
                continue

            action = eval_result.get("action", "maintain")
            entry: Dict[str, Any] = {"evaluation": eval_result}

            if action == "adjust_campaign":
                adj = self.adjust_campaign(tier)
                entry["adjustment"] = adj.to_dict() if adj else None
            elif action == "propose_paid_ads":
                # Propose paid campaign — budget = 10x monthly tier revenue
                tier_rev = {
                    "community": 500, "pro": 2000, "enterprise": 5000,
                    "creator_starter": 1000, "creator_pro": 2000,
                }
                budget = tier_rev.get(tier, 1000)
                proposal = self.propose_paid_campaign(tier, budget)
                entry["proposal"] = proposal.to_dict() if proposal else None

            results[tier] = entry

        return results

    # ------------------------------------------------------------------
    # Queries
    # ------------------------------------------------------------------

    def get_campaign(self, tier: str) -> Optional[Dict[str, Any]]:
        """Get full campaign state for a tier."""
        with self._lock:
            tc = self._campaigns.get(tier)
            return tc.to_dict() if tc else None

    def get_all_campaigns(self) -> Dict[str, Dict[str, Any]]:
        """Get all tier campaign summaries."""
        with self._lock:
            return {t: tc.to_dict() for t, tc in self._campaigns.items()}

    def get_proposals(
        self,
        status: Optional[ProposalStatus] = None,
    ) -> List[Dict[str, Any]]:
        """Get all paid-ad proposals, optionally filtered by status."""
        proposals: List[Dict[str, Any]] = []
        with self._lock:
            for tc in self._campaigns.values():
                for p in tc.proposals:
                    if status is None or p.status == status:
                        proposals.append(p.to_dict())
        return proposals

    def get_pending_approvals(self) -> List[Dict[str, Any]]:
        """Convenience: get all proposals awaiting founder approval."""
        return self.get_proposals(status=ProposalStatus.PENDING)

    def get_status(self) -> Dict[str, Any]:
        """System status summary."""
        with self._lock:
            total_adjustments = sum(
                len(tc.adjustments) for tc in self._campaigns.values()
            )
            total_proposals = sum(
                len(tc.proposals) for tc in self._campaigns.values()
            )
            pending = sum(
                1 for tc in self._campaigns.values()
                for p in tc.proposals if p.status == ProposalStatus.PENDING
            )
        return {
            "tier_campaign_count": len(self._campaigns),
            "total_adjustments": total_adjustments,
            "total_proposals": total_proposals,
            "pending_approvals": pending,
            "event_log_count": len(self._event_log),
        }

