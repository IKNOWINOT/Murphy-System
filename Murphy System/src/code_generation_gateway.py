# ---------------------------------------------------------------------------
# CodeGenerationGateway
# ---------------------------------------------------------------------------

class CodeGenerationGateway:
    """Safe, template-based code generation with validation.

    Design Label: ADV-001
    Owner: AI Team

    Usage::

        gw = CodeGenerationGateway(event_backbone=backbone)
        request = gw.generate(
            language="python",
            template_name="python_module",
            description="A data processor for CSV files",
            parameters={"class_name": "CsvProcessor"},
        )
        assert request.status.value == "validated"
    """

    def __init__(
        self,
        persistence_manager=None,
        event_backbone=None,
        improvement_engine=None,
    ) -> None:
        self._lock = threading.Lock()
        self._pm = persistence_manager
        self._backbone = event_backbone
        self._engine = improvement_engine
        self._requests: Dict[str, CodeGenRequest] = {}
        self._templates: Dict[str, str] = dict(_TEMPLATES)

    # ------------------------------------------------------------------
    # Template management
    # ------------------------------------------------------------------

    def register_template(self, name: str, template: str) -> None:
        """Register a custom code generation template."""
        with self._lock:
            self._templates[name] = template

    def list_templates(self) -> List[str]:
        """List available template names."""
        with self._lock:
            return list(self._templates.keys())

    # ------------------------------------------------------------------
    # Code generation
    # ------------------------------------------------------------------

    def generate(
        self,
        language: str,
        description: str,
        template_name: str = "python_module",
        parameters: Optional[Dict[str, Any]] = None,
    ) -> CodeGenRequest:
        """Generate code from template and validate it."""
        params = parameters or {}
        params.setdefault("description", description)
        params.setdefault("timestamp", datetime.now(timezone.utc).isoformat())

        request = CodeGenRequest(
            request_id=f"gen-{uuid.uuid4().hex[:8]}",
            language=language,
            description=description,
            template_name=template_name,
            parameters=params,
        )

        # Generate from template
        with self._lock:
            template = self._templates.get(template_name)

        if template is None:
            request.status = GenStatus.FAILED_VALIDATION
            request.validation_errors = [f"Unknown template: {template_name}"]
        else:
            try:
                code = template.format(**params)
                request.generated_code = code
                request.status = GenStatus.GENERATED
            except KeyError as exc:
                request.status = GenStatus.FAILED_VALIDATION
                request.validation_errors = [f"Missing template parameter: {exc}"]

        # Validate generated code
        if request.status == GenStatus.GENERATED:
            errors = self._validate_code(request.generated_code, language)
            if errors:
                request.status = GenStatus.FAILED_VALIDATION
                request.validation_errors = errors
            else:
                request.status = GenStatus.VALIDATED

        with self._lock:
            self._requests[request.request_id] = request

        # Persist
        if self._pm is not None:
            try:
                self._pm.save_document(
                    doc_id=request.request_id,
                    document=request.to_dict(),
                )
            except Exception as exc:
                logger.debug("Persistence skipped: %s", exc)

        # Publish event
        self._publish(request)

        logger.info(
            "Code gen %s: template=%s status=%s",
            request.request_id, template_name, request.status.value,
        )
        return request

    # ------------------------------------------------------------------
    # Query
    # ------------------------------------------------------------------

    def get_request(self, request_id: str) -> Optional[Dict[str, Any]]:
        with self._lock:
            req = self._requests.get(request_id)
        return req.to_dict() if req else None

    def list_requests(self, limit: int = 50) -> List[Dict[str, Any]]:
        with self._lock:
            reqs = list(self._requests.values())
        reqs.sort(key=lambda r: r.created_at, reverse=True)
        return [r.to_dict() for r in reqs[:limit]]

    def get_status(self) -> Dict[str, Any]:
        with self._lock:
            total = len(self._requests)
            by_status = {}
            for r in self._requests.values():
                by_status[r.status.value] = by_status.get(r.status.value, 0) + 1
        return {
            "total_requests": total,
            "by_status": by_status,
            "templates_available": list(self._templates.keys()),
            "persistence_attached": self._pm is not None,
            "backbone_attached": self._backbone is not None,
        }

    # ------------------------------------------------------------------
    # Validation
    # ------------------------------------------------------------------

    @staticmethod
    def _validate_code(code: str, language: str) -> List[str]:
        """Validate generated code for safety and correctness."""
        errors: List[str] = []

        # Check forbidden patterns
        for pattern in _FORBIDDEN_PATTERNS:
            if re.search(pattern, code):
                errors.append(f"Forbidden pattern detected: {pattern}")

        # Language-specific syntax check
        if language.lower() == "python":
            try:
                ast.parse(code)
            except SyntaxError as exc:
                errors.append(f"Python syntax error: {exc}")

        return errors

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _publish(self, request: CodeGenRequest) -> None:
        if self._backbone is not None:
            try:
                from event_backbone import EventType
                self._backbone.publish(
                    event_type=EventType.LEARNING_FEEDBACK,
                    payload={
                        "source": "code_generation_gateway",
                        "request_id": request.request_id,
                        "status": request.status.value,
                        "language": request.language,
                        "template": request.template_name,
                    },
                    source="code_generation_gateway",
                )
            except Exception as exc:
                logger.debug("EventBackbone publish skipped: %s", exc)
