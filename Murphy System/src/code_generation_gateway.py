from __future__ import annotations

import ast
import logging
import re
import threading
import uuid
from dataclasses import dataclass, field
from datetime import datetime, timezone
from enum import Enum
from typing import Any, Dict, List, Optional

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Safety: forbidden patterns
# ---------------------------------------------------------------------------

_FORBIDDEN_PATTERNS: List[str] = [
    r"\beval\s*\(",
    r"\bexec\s*\(",
    r"\bos\.system\s*\(",
    r"\bsubprocess\.",
    r"\b__import__\s*\(",
    r"\bcompile\s*\(",
    r"\bglobals\s*\(\s*\)",
    r"\blocals\s*\(\s*\)",
    r"\bopen\s*\(.*(w|a|x)",       # write-mode file opens
]


# ---------------------------------------------------------------------------
# Data models
# ---------------------------------------------------------------------------

class GenStatus(str, Enum):
    """Generation request lifecycle states."""
    PENDING = "pending"
    GENERATED = "generated"
    VALIDATED = "validated"
    FAILED_VALIDATION = "failed_validation"
    REJECTED = "rejected"


@dataclass
class CodeGenRequest:
    """A code generation request."""
    request_id: str
    language: str
    description: str
    template_name: str = "default"
    parameters: Dict[str, Any] = field(default_factory=dict)
    status: GenStatus = GenStatus.PENDING
    generated_code: str = ""
    validation_errors: List[str] = field(default_factory=list)
    created_at: str = field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )

    def to_dict(self) -> Dict[str, Any]:
        return {
            "request_id": self.request_id,
            "language": self.language,
            "description": self.description,
            "template_name": self.template_name,
            "parameters": dict(self.parameters),
            "status": self.status.value,
            "generated_code": self.generated_code,
            "validation_errors": list(self.validation_errors),
            "created_at": self.created_at,
        }


