class ExecutiveBotManager:
    """Manages all executive bots and their coordination"""
    
    def __init__(self, llm_client=None):
        self.llm_client = llm_client
        self.bots: Dict[BotRole, ExecutiveBot] = {}
        self._initialize_bots()
        logger.info("Executive Bot Manager initialized")
    
    def _initialize_bots(self):
        """Initialize all executive bots"""
        self.bots[BotRole.CEO] = CEOBot(self.llm_client)
        self.bots[BotRole.CTO] = CTOBot(self.llm_client)
        self.bots[BotRole.CFO] = CFOBot(self.llm_client)
    
    def get_bot(self, role: str) -> Optional[ExecutiveBot]:
        """Get a bot by role name"""
        for bot_role, bot in self.bots.items():
            if bot_role.value.lower() == role.lower():
                return bot
        return None
    
    def execute_command(self, bot_role: str, command: str, context: str = "") -> Dict[str, Any]:
        """Execute a command on a specific bot"""
        bot = self.get_bot(bot_role)
        if not bot:
            return {
                "status": "error",
                "message": f"Bot not found: {bot_role}"
            }
        
        return bot.execute_command(command, context)
    
    def coordinate_workflow(self, workflow_name: str) -> Dict[str, Any]:
        """
        Coordinate a workflow across multiple bots
        
        Example: executive_planning workflow
        1. CEO creates business plan
        2. CEO hands off to CTO
        3. CTO creates technical plan
        4. CTO hands off to CFO
        5. CFO creates financial plan
        6. CFO hands off to CEO for final approval
        """
        
        results = []
        
        if workflow_name == "executive_planning":
            # Step 1: CEO creates business plan
            ceo_plan = self.bots[BotRole.CEO].generate_business_plan()
            results.append({
                "bot": "CEO",
                "action": "generate_business_plan",
                "result": ceo_plan.__dict__
            })
            
            # Step 2: CEO hands off to CTO
            handoff1 = self.bots[BotRole.CEO].handoff_to(
                "CTO",
                "Business objectives for technical planning",
                ["Business Plan", "Revenue Targets", "Growth Metrics"]
            )
            results.append(handoff1)
            self.bots[BotRole.CTO].receive_handoff(handoff1["handoff"])
            
            # Step 3: CTO creates technical plan
            cto_plan = self.bots[BotRole.CTO].generate_technical_plan()
            results.append({
                "bot": "CTO",
                "action": "generate_technical_plan",
                "result": cto_plan.__dict__
            })
            
            # Step 4: CTO hands off to CFO
            handoff2 = self.bots[BotRole.CTO].handoff_to(
                "CFO",
                "Technical requirements and budget needs",
                ["Technical Plan", "Infrastructure Requirements", "Resource Needs"]
            )
            results.append(handoff2)
            self.bots[BotRole.CFO].receive_handoff(handoff2["handoff"])
            
            # Step 5: CFO creates financial plan
            cfo_plan = self.bots[BotRole.CFO].generate_financial_plan()
            results.append({
                "bot": "CFO",
                "action": "generate_financial_plan",
                "result": cfo_plan.__dict__
            })
            
            # Step 6: CFO hands off to CEO for approval
            handoff3 = self.bots[BotRole.CFO].handoff_to(
                "CEO",
                "Financial plan for final approval",
                ["Financial Plan", "Budget Allocation", "ROI Projections"]
            )
            results.append(handoff3)
            self.bots[BotRole.CEO].receive_handoff(handoff3["handoff"])
        
        return {
            "workflow": workflow_name,
            "status": "completed",
            "results": results,
            "message": f"Executive planning workflow completed across {len(results)} steps"
        }
    
    def get_bot_terminology(self, bot_role: str) -> List[str]:
        """Get domain terminology for a bot"""
        bot = self.get_bot(bot_role)
        if bot and hasattr(bot, 'domain_terminology'):
            return bot.domain_terminology
        return []
    
    def get_all_bots(self) -> List[Dict[str, Any]]:
        """Get information about all bots"""
        return [
            {
                "role": role.value,
                "type": "executive",
                "domain": bot.domain_terminology[0] if bot.domain_terminology else "general"
            }
            for role, bot in self.bots.items()
        ]
    
    def get_workflow_status(self) -> Dict[str, Any]:
        """Get status of all workflow executions"""
        return {
            "total_bots": len(self.bots),
            "commands_executed": sum(len(bot.commands_executed) for bot in self.bots.values()),
            "decisions_made": sum(len(bot.decisions_made) for bot in self.bots.values()),
            "handoffs": {
                "sent": sum(len(bot.handoffs_sent) for bot in self.bots.values()),
                "received": sum(len(bot.handoffs_received) for bot in self.bots.values())
            }
        }