class DecisionStatus(Enum):
    """Status of executive decisions"""
    PENDING = "pending"
    IN_PROGRESS = "in_progress"
    APPROVED = "approved"
    REJECTED = "rejected"
    REQUIRES_REVIEW = "requires_review"


@dataclass
class BusinessPlan:
    """A business plan generated by executive bots"""
    executive: str
    domain: str
    timeframe: str
    objectives: List[str]
    strategies: List[str]
    kpis: List[str]
    resources: Dict[str, Any]
    risks: List[str]
    created_at: str = field(default_factory=lambda: datetime.now().isoformat())
    status: str = "draft"


@dataclass
class ExecutiveDecision:
    """A decision made by an executive bot"""
    bot: str
    topic: str
    decision: str
    rationale: str
    status: DecisionStatus
    dependencies: List[str] = field(default_factory=list)
    handoffs: List[Dict[str, str]] = field(default_factory=list)
    timestamp: str = field(default_factory=lambda: datetime.now().isoformat())


@dataclass
class BotCommand:
    """A command executed by a bot"""
    bot: str
    command: str
    context: str
    result: Optional[str] = None
    status: str = "pending"
    timestamp: str = field(default_factory=lambda: datetime.now().isoformat())


class ExecutiveBot:
    """Base class for executive bots"""
    
    def __init__(self, role: BotRole, llm_client=None):
        self.role = role
        self.llm_client = llm_client
        self.commands_executed: List[BotCommand] = []
        self.decisions_made: List[ExecutiveDecision] = []
        self.handoffs_sent: List[Dict[str, str]] = []
        self.handoffs_received: List[Dict[str, str]] = []
        
        logger.info(f"Executive bot initialized: {role.value}")
    
    def execute_command(self, command: str, context: str = "") -> Dict[str, Any]:
        """Execute a command in the bot's domain"""
        
        # Parse command
        cmd_parts = self._parse_command(command)
        cmd_action = cmd_parts.get("action", "")
        cmd_args = cmd_parts.get("args", "")
        cmd_comment = cmd_parts.get("comment", "")
        
        # Execute based on action
        result = self._execute_action(cmd_action, cmd_args, cmd_comment, context)
        
        # Record command execution
        bot_command = BotCommand(
            bot=self.role.value,
            command=command,
            context=context,
            result=result.get("output", ""),
            status=result.get("status", "completed")
        )
        self.commands_executed.append(bot_command)
        
        return result
    
    def _parse_command(self, command: str) -> Dict[str, str]:
        """Parse a command into components"""
        # Format: /action args #comment
        parts = command.split()
        if not parts:
            return {}
        
        action = parts[0].replace("/", "").strip()
        
        # Find comment
        comment = ""
        args = ""
        comment_idx = -1
        for i, part in enumerate(parts):
            if part.startswith("#"):
                comment_idx = i
                comment = " ".join(parts[i:])[1:].strip()
                break
        
        # Get args (between action and comment)
        if comment_idx > 1:
            args = " ".join(parts[1:comment_idx])
        elif comment_idx == -1 and len(parts) > 1:
            args = " ".join(parts[1:])
        
        return {
            "action": action,
            "args": args,
            "comment": comment
        }
    
    def _execute_action(self, action: str, args: str, comment: str, context: str) -> Dict[str, Any]:
        """Execute a specific action (to be overridden by subclasses)"""
        return {
            "status": "completed",
            "output": f"{self.role.value} executed: {action} {args}",
            "bot": self.role.value,
            "action": action,
            "args": args
        }
    
    def make_decision(self, topic: str, options: List[str], context: str = "") -> ExecutiveDecision:
        """Make an executive decision"""
        
        # Use LLM if available for decision making
        if self.llm_client:
            decision_text = self._llm_decision(topic, options, context)
        else:
            # Fallback: use domain expertise
            decision_text = self._rule_based_decision(topic, options, context)
        
        decision = ExecutiveDecision(
            bot=self.role.value,
            topic=topic,
            decision=decision_text,
            rationale=f"Decision based on {self.role.value} domain expertise",
            status=DecisionStatus.APPROVED
        )
        
        self.decisions_made.append(decision)
        return decision
    
    def _llm_decision(self, topic: str, options: List[str], context: str) -> str:
        """Use LLM to make a decision"""
        try:
            prompt = f"""
As the {self.role.value} of this company, please make a decision on:

Topic: {topic}
Options:
{chr(10).join(f"{i+1}. {opt}" for i, opt in enumerate(options))}

Context: {context}

Please select the best option and provide a brief rationale.
"""
            response = self.llm_client.generate(prompt)
            return response.strip()
        except Exception as e:
            logger.error(f"LLM decision failed: {e}")
            return options[0] if options else "No decision"
    
    def _rule_based_decision(self, topic: str, options: List[str], context: str) -> str:
        """Rule-based decision making (fallback)"""
        # Simple heuristic: return first option
        return options[0] if options else "No decision available"
    
    def handoff_to(self, target_bot: str, context: str, artifacts: List[str]) -> Dict[str, str]:
        """Hand off work to another bot"""
        handoff = {
            "from": self.role.value,
            "to": target_bot,
            "context": context,
            "artifacts": artifacts,
            "timestamp": datetime.now().isoformat()
        }
        
        self.handoffs_sent.append(handoff)
        
        return {
            "status": "handoff_complete",
            "handoff": handoff,
            "message": f"{self.role.value} handed off to {target_bot}"
        }
    
    def receive_handoff(self, handoff: Dict[str, str]) -> None:
        """Receive a handoff from another bot"""
        self.handoffs_received.append(handoff)
        logger.info(f"{self.role.value} received handoff from {handoff['from']}")


