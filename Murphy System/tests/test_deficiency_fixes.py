class TestDEF002_CSPHeaders:
    """Content-Security-Policy must allow Swagger UI CDN resources."""

    def test_csp_allows_jsdelivr_scripts(self):
        from src.fastapi_security import _SECURITY_HEADERS
        csp = _SECURITY_HEADERS["Content-Security-Policy"]
        assert "https://cdn.jsdelivr.net" in csp, (
            "CSP script-src must whitelist cdn.jsdelivr.net for Swagger UI JS"
        )

    def test_csp_allows_jsdelivr_styles(self):
        from src.fastapi_security import _SECURITY_HEADERS
        csp = _SECURITY_HEADERS["Content-Security-Policy"]
        style_match = re.search(r"style-src\s+([^;]+)", csp)
        assert style_match, "CSP must have a style-src directive"
        assert "cdn.jsdelivr.net" in style_match.group(1)

    def test_csp_allows_fastapi_favicon(self):
        from src.fastapi_security import _SECURITY_HEADERS
        csp = _SECURITY_HEADERS["Content-Security-Policy"]
        img_match = re.search(r"img-src\s+([^;]+)", csp)
        assert img_match, "CSP must have an img-src directive"
        assert "fastapi.tiangolo.com" in img_match.group(1)

    def test_csp_allows_inline_scripts(self):
        """Swagger UI injects inline scripts; CSP must allow them."""
        from src.fastapi_security import _SECURITY_HEADERS
        csp = _SECURITY_HEADERS["Content-Security-Policy"]
        script_match = re.search(r"script-src\s+([^;]+)", csp)
        assert script_match, "CSP must have a script-src directive"
        assert "'unsafe-inline'" in script_match.group(1)


