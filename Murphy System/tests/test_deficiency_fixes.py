class TestLibrarianAlwaysActive:
    """Librarian must always be operational — onboard mode or LLM mode."""

    @pytest.fixture(autouse=True)
    def _setup(self):
        self.murphy = MurphySystem()

    def test_librarian_status_enabled_without_llm(self):
        """Even without LLM API key, librarian reports enabled=True."""
        with patch.dict(os.environ, {}, clear=True):
            status = self.murphy._get_librarian_status()
        assert status["enabled"] is True, "Librarian must always be enabled"
        assert status["mode"] == "onboard", (
            "Without LLM API key, librarian should be in 'onboard' mode"
        )

    def test_librarian_status_mode_llm_with_key(self):
        """With LLM API key set, librarian reports mode='llm'."""
        with patch.dict(os.environ, {
            "MURPHY_LLM_PROVIDER": "groq",
            "GROQ_API_KEY": "test_key"
        }):
            status = self.murphy._get_librarian_status()
        assert status["enabled"] is True
        assert status["mode"] == "llm"
        assert status["llm_provider"] == "groq"

    def test_librarian_ask_works_without_llm(self):
        """Librarian must respond in onboard mode without any LLM key."""
        with patch.dict(os.environ, {}, clear=True):
            result = self.murphy.librarian_ask("How do I get started?")
        assert result["success"] is True, "Librarian must always succeed in onboard mode"
        assert result["mode"] == "onboard"
        assert result["message"], "Librarian onboard response must not be empty"
        assert result["response"], "Librarian onboard response field must not be empty"

    def test_librarian_ask_returns_response_field(self):
        """librarian_ask response must have both 'response' and 'message'."""
        result = self.murphy.librarian_ask("help")
        assert "response" in result
        assert "message" in result
        assert result["response"] == result["message"]

    def test_librarian_mode_shown_in_system_status(self):
        """System status should show librarian mode."""
        status = self.murphy.get_system_status()
        lib = status.get("librarian", {})
        assert lib.get("enabled") is True
        assert "mode" in lib

