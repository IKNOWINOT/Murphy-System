# DEF-003: Blocked Execution Returns Clarifying Questions
# ===========================================================================


class TestDEF003_ClarifyingQuestions:
    """When execution is blocked, the response must include clarifying questions."""

    @pytest.fixture(autouse=True)
    def _setup(self):
        self.murphy = MurphySystem()

    @pytest.mark.asyncio
    async def test_blocked_response_has_clarifying_questions(self):
        """Submitting a vague task should return clarifying_questions."""
        result = await self.murphy.execute_task(
            task_description="Generate a sales report for Q4",
            task_type="general",
        )
        assert result["success"] is False
        assert "clarifying_questions" in result, (
            "Blocked response must include 'clarifying_questions' to help "
            "the user provide information that raises confidence."
        )
        questions = result["clarifying_questions"]
        assert isinstance(questions, list)
        assert len(questions) > 0, (
            "At least one clarifying question must be returned when execution "
            "is blocked so the user knows what information to provide."
        )

    @pytest.mark.asyncio
    async def test_clarifying_questions_have_required_fields(self):
        """Each clarifying question must have id, question, and category."""
        result = await self.murphy.execute_task(
            task_description="Do something",
            task_type="general",
        )
        if result.get("clarifying_questions"):
            for q in result["clarifying_questions"]:
                assert "id" in q, "Each question must have an 'id'"
                assert "question" in q, "Each question must have a 'question' text"
                assert "category" in q, "Each question must have a 'category'"

    def test_onboard_clarifying_questions_from_information_gaps(self):
        """_onboard_clarifying_questions should extract from information_gaps."""
        activation_preview = {
            "dynamic_implementation": {
                "information_gaps": [
                    {"id": "requirements_identification", "label": "Requirements",
                     "owner": "user", "reason": "Missing onboarding answers."},
                ],
                "next_actions": ["Collect missing onboarding answers to lock requirements."],
            },
            "confidence": 0.3,
        }
        questions = self.murphy._onboard_clarifying_questions(
            "some task", activation_preview
        )
        assert len(questions) > 0
        gap_qs = [q for q in questions if q["category"] == "information_gap"]
        assert len(gap_qs) >= 1

    def test_onboard_clarifying_questions_fallback_for_low_confidence(self):
        """When confidence is very low and no gaps, generate generic questions."""
        activation_preview = {
            "dynamic_implementation": {
                "information_gaps": [],
                "next_actions": [],
            },
            "confidence": 0.2,
        }
        questions = self.murphy._onboard_clarifying_questions(
            "some vague task", activation_preview
        )
        assert len(questions) >= 3, (
            "With very low confidence and no gaps, fallback questions should be generated"
        )
        categories = {q["category"] for q in questions}
        assert "task_clarification" in categories

    def test_llm_question_generation_returns_empty_without_api(self):
        """Without an LLM API key, _try_llm_clarifying_questions returns empty list."""
        with patch.dict(os.environ, {}, clear=True):
            activation_preview = {
                "dynamic_implementation": {
                    "information_gaps": [],
                    "next_actions": [],
                },
                "confidence": 0.3,
            }
            questions = self.murphy._try_llm_clarifying_questions(
                "some task", activation_preview
            )
            assert questions == [], (
                "Without LLM API key, LLM question generation should return empty "
                "and fall back to onboard questions."
            )

    def test_build_clarifying_questions_delegates_correctly(self):
        """_build_clarifying_questions should use onboard when no LLM API."""
        with patch.dict(os.environ, {}, clear=True):
            activation_preview = {
                "dynamic_implementation": {
                    "information_gaps": [
                        {"id": "req", "label": "Requirements", "owner": "user",
                         "reason": "Describe your requirements."},
                    ],
                    "next_actions": [],
                },
                "confidence": 0.3,
            }
            questions = self.murphy._build_clarifying_questions(
                "some task", activation_preview
            )
            assert len(questions) > 0
            # Should be from onboard since no LLM
            assert any(q["category"] == "information_gap" for q in questions)


