    """Manifest entry for a single Murphy module."""

    module: str
    room: str
    commands: List[str] = field(default_factory=list)
    persona: str = "TriageBot"
    emits: List[str] = field(default_factory=list)
    consumes: List[str] = field(default_factory=list)
    description: str = ""


