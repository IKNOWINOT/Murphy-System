"""Module Manifest — complete map of every Murphy module/subsystem to its
Matrix room, available commands, responsible bot persona, and event types.

This manifest is exhaustive: every ``.py`` file and every subdirectory
package under ``src/`` has an entry.

Each entry is a :class:`ModuleEntry` with:
- ``module``       — Python import path relative to ``src/``
- ``room``         — Matrix subsystem room key (matches ``SUBSYSTEM_ROOMS``)
- ``commands``     — list of ``!murphy`` command strings
- ``persona``      — responsible bot persona name
- ``emits``        — event types the module emits
- ``consumes``     — event types the module consumes
"""

from __future__ import annotations
import logging

from dataclasses import dataclass, field
from typing import Dict, List, Optional


