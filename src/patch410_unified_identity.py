"""
PATCH-410 — Unified Identity Service (Device Pairing + Capability Manifest)
============================================================================

WHAT THIS IS:
  The bridge between Murphy's existing OAuth2/OIDC server (already at
  /oauth/* — see murphy_oauth_server.py and oauth_oidc_provider.py) and
  Murphy's household profile registry (PATCH-408). It adds three things
  the existing OAuth server didn't have:

    1. DEVICE PAIRING — A logged-in surface (desktop web at /login) can
       generate a 6-digit PIN + QR code. A fresh device (Android phone,
       PiCar-X robot, Electron desktop on a new machine) shows that PIN
       on its own setup screen, the user confirms on the source device,
       and the new device gets a long-lived refresh token tied to a
       household profile.

    2. PROFILE BRIDGE — The OAuth `sub` claim is mapped to a
       household_profiles.profile_id (PATCH-408). This means a single
       voice fingerprint, a single device login, and a single API call
       all resolve to the same person with the same permission tier.

    3. CAPABILITY MANIFEST — All three client surfaces (Desktop, Android,
       PiCar-X) call /api/identity/capabilities on every boot. The server
       returns the canonical list of features, command verbs, and
       permission gates THAT VERSION of the client should expose. This
       enforces Corey's "capability should match on desktop and Android"
       directive by making capability a server-controlled artifact, not
       a per-client hard-coded list.

WHY IT EXISTS:
  Corey's directive 2026-05-24: "take dispatch requests via credential
  matching with a desktop app, Android phone app, and picar-x operating
  system that controls the entire picar x. capability should match on the
  desktop and android app."

  Before this patch:
    - OAuth server existed but had no concept of "device" (only "client app")
    - Household profiles existed but had no token system
    - There was no canonical capability list — each client would drift

  After this patch:
    - One identity (profile_id) → many devices (phone, desktop, PiCar)
    - One pairing flow works for all three surfaces
    - One capability manifest keeps desktop + Android synchronized
    - PiCar-X gets its own permission tier (robot_body) that lets it act
      on behalf of whichever profile its current speaker is identified as

HOW IT FITS:
  - Sits at Layer 3 (Identity) above PATCH-408 (Household)
  - Depends on:
      • oauth_oidc_provider.py (existing) for OIDC primitives
      • murphy_oauth_server.py (existing) for /oauth/* routes
      • PATCH-408 household_profiles table (this session)
      • PATCH-405 vault (encrypted refresh-token storage)
  - Consumed by:
      • PATCH-411 PiCar-X OS (Pi client uses /api/identity/device/pair)
      • PATCH-412 Desktop app (Electron uses same pair flow)
      • PATCH-413 Android app (RN uses same pair flow)
      • Future per-feature gates check this layer first

KEY CONCEPTS:
  - Pairing code: 6-digit numeric, expires 5 min, single-use.
    Generated by a logged-in "source" device, claimed by a "target" device.
  - Device record: device_id, device_type (desktop|android|ios|picarx|other),
    paired_profile_id, refresh_token_jti, last_seen_at, capabilities granted.
  - Refresh token: 64-char opaque, stored hashed in DB, encrypted in vault.
  - Access token: JWT signed by existing OIDC JWKS, 15-min lifetime,
    claims include profile_id, permission_tier, household role.
  - Capability manifest: server-rendered JSON of feature flags per surface.
    Versioned. Clients respect "min_client_version" hints.

ENDPOINTS / PUBLIC SURFACE:
  POST  /api/identity/device/pair/start    — source device generates 6-digit PIN
  POST  /api/identity/device/pair/claim    — target device redeems PIN
  POST  /api/identity/device/pair/confirm  — source device confirms claim
  GET   /api/identity/devices              — list devices for current profile
  DELETE /api/identity/device/{device_id}  — revoke a device (kill its tokens)
  POST  /api/identity/token/refresh        — exchange refresh for access
  GET   /api/identity/me                   — current profile + permissions
  GET   /api/identity/capabilities         — capability manifest for caller's surface
  POST  /api/identity/voice-login          — PiCar identifies speaker → issues access token
                                              tied to that profile (no PIN needed,
                                              uses voice fingerprint from PATCH-408)

  GET   /devices                            — HTML UI to manage paired devices

DEPENDENCIES:
  - sqlite3, json, hashlib, hmac, secrets, time (stdlib)
  - jwt (PyJWT — already in venv for oidc_verifier)
  - PATCH-408 patch408_household_picarx (profile lookup)
  - PATCH-405 patch405_secrets_vault (refresh token encryption — optional,
    falls back to hashed-only storage if vault unreachable)

VAULT SECRETS USED:
  None mandatory. Optionally stores refresh tokens encrypted via PATCH-405.

EVENT SPINE EMISSIONS:
  - device_pair_started
  - device_pair_claimed
  - device_pair_confirmed
  - device_pair_failed
  - device_revoked
  - token_refresh_issued
  - voice_login_issued

KNOWN LIMITS:
  - PIN brute force: 6 digits = 1M space, single-use, 5-min expiry, 5-try
    lockout per source PIN. Adequate; not fort-knox.
  - JWT signing key: reuses existing OIDC JWKS (no key rotation in this
    patch — defer to P-420 PQC migration).
  - Capability manifest is static-but-versioned; future P-415 will let
    Murphy ship new versions of the manifest atomically.

LAST UPDATED: 2026-05-24 by Murphy (claude-sonnet-4-5)
"""
