# ── Helpers ─────────────────────────────────────────────────────────────────
def _db():
    conn = sqlite3.connect(DB_PATH, timeout=30, check_same_thread=False)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.row_factory = sqlite3.Row
    return conn


def _now() -> str:
    return datetime.now(timezone.utc).isoformat()


def _ts_to_iso(ts: float) -> str:
    return datetime.fromtimestamp(ts, tz=timezone.utc).isoformat()


def _gid(prefix: str) -> str:
    return f"{prefix}_{secrets.token_hex(10)}"


def _hash(text: str) -> str:
    return hashlib.sha256(text.encode()).hexdigest()


def _hmac_hash(secret: str, text: str) -> str:
    return hmac.new(secret.encode(), text.encode(), hashlib.sha256).hexdigest()


def _get_jwt_secret() -> str:
    """
    Load (or create) the identity JWT signing secret. Stored 0400 root-only
    on engine. NOT the same as the OIDC JWKS — those are RSA. This secret
    is HS256 for symmetric, server-internal access tokens (faster verify,
    fine for our use because the server is both issuer and verifier).
    """
    if os.path.exists(JWT_SECRET_PATH):
        try:
            with open(JWT_SECRET_PATH, "r") as f:
                return f.read().strip()
        except PermissionError:
            # We're running as 'murphy' not root — secret should be readable
            # via group 'murphy' if perms set to 0440 root:murphy
            try:
                with open(JWT_SECRET_PATH, "r") as f:
                    return f.read().strip()
            except Exception:
                pass
    # First-run: generate
    secret = secrets.token_urlsafe(64)
    try:
        os.makedirs(os.path.dirname(JWT_SECRET_PATH), exist_ok=True)
        with open(JWT_SECRET_PATH, "w") as f:
            f.write(secret)
        os.chmod(JWT_SECRET_PATH, 0o440)
    except PermissionError:
        log.warning("Could not persist JWT secret to %s — using in-memory only "
                    "(will rotate on restart!)", JWT_SECRET_PATH)
    return secret


_JWT_SECRET: Optional[str] = None
def jwt_secret() -> str:
    global _JWT_SECRET
    if _JWT_SECRET is None:
        _JWT_SECRET = _get_jwt_secret()
    return _JWT_SECRET


