def voice_login(picarx_device_id: str, audio_path: str,
                ip: Optional[str] = None) -> Dict[str, Any]:
    """
    PiCar identifies a speaker via voice fingerprint (PATCH-408), then asks
    Murphy to issue an access token AS THAT SPEAKER, scoped to the PiCar
    device. This is what lets "Kaylyn says 'send me a dinosaur picture'"
    deliver to kaylyn.post@murphy.systems and not corey's inbox.
    """
    from src.patch408_household_picarx import identify_speaker, get_profile  # type: ignore
    ident = identify_speaker(audio_path)
    if not ident.get("ok"):
        return {"ok": False, "error": ident.get("error", "speaker_id_failed")}
    if ident.get("status") != "confident":
        return {"ok": False, "error": "speaker_unrecognized",
                "status": ident.get("status"),
                "confidence": ident.get("confidence"),
                "candidates": ident.get("candidates")}
    profile_id = ident["profile_id"]
    prof = get_profile(profile_id)
    if not prof:
        return {"ok": False, "error": "profile_missing"}
    access = mint_access_token(
        profile_id, picarx_device_id, prof["permission_tier"], "picarx",
        lifetime_s=VOICE_LOGIN_TOKEN_LIFETIME_SECONDS)
    _audit("voice_login", device_id=picarx_device_id,
           profile_id=profile_id, ip=ip,
           detail=f"confidence={ident.get('confidence'):.3f}")
    _emit_event("voice_login_issued",
                device_id=picarx_device_id, profile_id=profile_id,
                confidence=ident.get("confidence"))
    return {
        "ok": True,
        "profile_id": profile_id,
        "preferred_name": prof.get("preferred_name") or prof["full_name"],
        "permission_tier": prof["permission_tier"],
        "access_token": access["access_token"],
        "token_type": "Bearer",
        "expires_in": VOICE_LOGIN_TOKEN_LIFETIME_SECONDS,
        "confidence": ident.get("confidence"),
    }


