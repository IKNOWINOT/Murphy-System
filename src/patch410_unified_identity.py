def list_devices_for_profile(profile_id: str) -> List[Dict[str, Any]]:
    conn = _db()
    try:
        rows = conn.execute("""
            SELECT device_id, device_name, device_type, paired_at,
                   last_seen_at, last_ip, revoked_at, revoke_reason
            FROM devices WHERE profile_id=?
            ORDER BY paired_at DESC
        """, (profile_id,)).fetchall()
        return [dict(r) for r in rows]
    finally:
        conn.close()


def revoke_device(device_id: str, reason: str = "user_request") -> Dict[str, Any]:
    conn = _db()
    try:
        cur = conn.execute("""
            UPDATE devices SET revoked_at=?, revoke_reason=?
            WHERE device_id=? AND revoked_at IS NULL
        """, (_now(), reason, device_id))
        conn.commit()
        if cur.rowcount == 0:
            return {"ok": False, "error": "not_found_or_already_revoked"}
    finally:
        conn.close()
    _audit("device_revoke", device_id=device_id, detail=reason)
    _emit_event("device_revoked", device_id=device_id, reason=reason)
    return {"ok": True, "revoked": device_id}


