@dataclass
class HeartbeatPolicy:
    """Heartbeat policy for a bot."""
    interval_seconds: float = 30.0
    max_missed: int = 3
    recovery_strategy: str = "restart"  # restart | alert | escalate


@dataclass
class BotManifest:
    """Declares a single bot's desired state."""
    bot_id: str
    name: str
    role: str  # maps to BotRole enum value
    capabilities: List[str] = field(default_factory=list)
    heartbeat_policy: HeartbeatPolicy = field(default_factory=HeartbeatPolicy)
    governance: Dict[str, Any] = field(default_factory=dict)
    dependencies: List[str] = field(default_factory=list)  # bot_ids started first
    replicas: int = 1
    enabled: bool = True

    def to_dict(self) -> Dict[str, Any]:
        return {
            "bot_id": self.bot_id,
            "name": self.name,
            "role": self.role,
            "capabilities": self.capabilities,
            "heartbeat_policy": {
                "interval_seconds": self.heartbeat_policy.interval_seconds,
                "max_missed": self.heartbeat_policy.max_missed,
                "recovery_strategy": self.heartbeat_policy.recovery_strategy,
            },
            "governance": self.governance,
            "dependencies": self.dependencies,
            "replicas": self.replicas,
            "enabled": self.enabled,
        }


@dataclass
class FleetManifest:
    """Declares the entire desired fleet state."""
    fleet_id: str
    version: str
    description: str
    bots: List[BotManifest] = field(default_factory=list)
    global_policies: Dict[str, Any] = field(default_factory=dict)
    supervision_topology: Dict[str, List[str]] = field(default_factory=dict)
    created_at: str = field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )
    updated_at: str = field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )

    def to_dict(self) -> Dict[str, Any]:
        return {
            "fleet_id": self.fleet_id,
            "version": self.version,
            "description": self.description,
            "bots": [b.to_dict() for b in self.bots],
            "global_policies": self.global_policies,
            "supervision_topology": self.supervision_topology,
            "created_at": self.created_at,
            "updated_at": self.updated_at,
        }


@dataclass
class ReconciliationAction:
    """A single action generated by the diff engine."""
    action_id: str = field(default_factory=lambda: f"act_{uuid.uuid4().hex[:8]}")
    action_type: ActionType = ActionType.SPAWN
    target_bot_id: str = ""
    details: Dict[str, Any] = field(default_factory=dict)
    status: ActionStatus = ActionStatus.PENDING

    def to_dict(self) -> Dict[str, Any]:
        return {
            "action_id": self.action_id,
            "action_type": self.action_type.value,
            "target_bot_id": self.target_bot_id,
            "details": self.details,
            "status": self.status.value,
        }


# ---------------------------------------------------------------------------
# Manifest validation helpers
# ---------------------------------------------------------------------------

class ManifestValidationError(ValueError):
    """Raised when a FleetManifest fails validation."""


def _topological_sort(bots: List[BotManifest]) -> List[BotManifest]:
    """
    Return bots in dependency-first order (topological sort).

    Raises ManifestValidationError if a dependency cycle is detected.
    """
    bot_map: Dict[str, BotManifest] = {b.bot_id: b for b in bots}
    order: List[BotManifest] = []
    visited: set = set()
    visiting: set = set()  # cycle detection

    def visit(bot_id: str) -> None:
        if bot_id in visited:
            return
        if bot_id in visiting:
            raise ManifestValidationError(
                f"Dependency cycle detected involving bot_id '{bot_id}'"
            )
        visiting.add(bot_id)
        bot = bot_map.get(bot_id)
        if bot is not None:
            for dep in bot.dependencies:
                visit(dep)
            visiting.discard(bot_id)
            visited.add(bot_id)
            order.append(bot)

    for b in bots:
        visit(b.bot_id)
    return order


def _validate_manifest(
    manifest: FleetManifest,
    capability_registry: Optional[Dict[str, Any]] = None,
) -> None:
    """
    Validate a FleetManifest.

    Checks:
    - replicas >= 0 for every bot
    - all dependency bot_ids exist within the manifest
    - no dependency cycles
    - if capability_registry provided, all listed capabilities must exist

    Raises ManifestValidationError on the first problem found.
    """
    known_ids = {b.bot_id for b in manifest.bots}

    for bot in manifest.bots:
        if bot.replicas < 0:
            raise ManifestValidationError(
                f"Bot '{bot.bot_id}' has negative replicas ({bot.replicas})"
            )
        for dep in bot.dependencies:
            if dep not in known_ids:
                raise ManifestValidationError(
                    f"Bot '{bot.bot_id}' references unknown dependency '{dep}'"
                )
        if capability_registry is not None:
            for cap in bot.capabilities:
                if cap not in capability_registry:
                    raise ManifestValidationError(
                        f"Bot '{bot.bot_id}' references unknown capability '{cap}'"
                    )

    # Check for cycles
    _topological_sort(manifest.bots)


