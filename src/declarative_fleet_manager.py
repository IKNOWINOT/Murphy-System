class ActionType(Enum):
    """Possible reconciliation actions."""
    SPAWN = "spawn"
    DESPAWN = "despawn"
    UPDATE = "update"
    REGISTER_HEARTBEAT = "register_heartbeat"
    WIRE_SUPERVISION = "wire_supervision"


class ActionStatus(Enum):
    """Status of a reconciliation action."""
    PENDING = "pending"
    COMPLETED = "completed"
    FAILED = "failed"


