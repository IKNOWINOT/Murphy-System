class ManifestLoader:
    """Load a FleetManifest from YAML, JSON, or a plain dict."""

    @staticmethod
    def _bot_from_dict(data: Dict[str, Any]) -> BotManifest:
        hp_data = data.get("heartbeat_policy", {})
        hp = HeartbeatPolicy(
            interval_seconds=float(hp_data.get("interval_seconds", 30.0)),
            max_missed=int(hp_data.get("max_missed", 3)),
            recovery_strategy=str(hp_data.get("recovery_strategy", "restart")),
        )
        return BotManifest(
            bot_id=str(data["bot_id"]),
            name=str(data.get("name", data["bot_id"])),
            role=str(data.get("role", "assistant")),
            capabilities=list(data.get("capabilities", [])),
            heartbeat_policy=hp,
            governance=dict(data.get("governance", {})),
            dependencies=list(data.get("dependencies", [])),
            replicas=int(data.get("replicas", 1)),
            enabled=bool(data.get("enabled", True)),
        )

    @classmethod
    def load_from_dict(
        cls,
        data: Dict[str, Any],
        capability_registry: Optional[Dict[str, Any]] = None,
    ) -> FleetManifest:
        """Build and validate a FleetManifest from a plain dictionary."""
        bots = [cls._bot_from_dict(b) for b in data.get("bots", [])]
        manifest = FleetManifest(
            fleet_id=str(data.get("fleet_id", f"fleet_{uuid.uuid4().hex[:8]}")),
            version=str(data.get("version", "1.0")),
            description=str(data.get("description", "")),
            bots=bots,
            global_policies=dict(data.get("global_policies", {})),
            supervision_topology=dict(data.get("supervision_topology", {})),
            created_at=str(
                data.get("created_at", datetime.now(timezone.utc).isoformat())
            ),
            updated_at=str(
                data.get("updated_at", datetime.now(timezone.utc).isoformat())
            ),
        )
        _validate_manifest(manifest, capability_registry)
        return manifest

    @classmethod
    def load_from_yaml(
        cls,
        path: str,
        capability_registry: Optional[Dict[str, Any]] = None,
    ) -> FleetManifest:
        """Load and validate a FleetManifest from a YAML file."""
        with open(path, encoding="utf-8") as fh:
            data = yaml.safe_load(fh)
        return cls.load_from_dict(data or {}, capability_registry)

    @classmethod
    def load_from_json(
        cls,
        path: str,
        capability_registry: Optional[Dict[str, Any]] = None,
    ) -> FleetManifest:
        """Load and validate a FleetManifest from a JSON file."""
        with open(path, encoding="utf-8") as fh:
            data = json.load(fh)
        return cls.load_from_dict(data, capability_registry)


