@dataclass
class DriftItem:
    """Describes a single drift between desired and actual state."""
    bot_id: str
    drift_type: str  # missing | extra | config_mismatch
    details: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "bot_id": self.bot_id,
            "drift_type": self.drift_type,
            "details": self.details,
        }


class FleetDriftDetector:
    """
    Continuously monitors for drift between declared and actual bot fleet.

    Can be wired into a heartbeat tick cycle or polled directly.
    """

    def __init__(
        self,
        manifest: Optional[FleetManifest] = None,
        bot_inventory: Optional[Any] = None,
    ) -> None:
        self._manifest = manifest
        self._bot_inventory = bot_inventory
        self._lock = threading.Lock()

    def update_manifest(self, manifest: FleetManifest) -> None:
        """Replace the current manifest."""
        with self._lock:
            self._manifest = manifest

    def check_drift(self) -> List[DriftItem]:
        """
        Compare declared state vs actual inventory state.

        Returns a list of DriftItem objects describing all detected drift.
        """
        with self._lock:
            manifest = self._manifest
            inventory = self._bot_inventory

        if manifest is None:
            return []

        drifts: List[DriftItem] = []

        if inventory is None:
            # No inventory — all enabled bots are missing
            for bot in manifest.bots:
                if bot.enabled and bot.replicas > 0:
                    drifts.append(
                        DriftItem(
                            bot_id=bot.bot_id,
                            drift_type="missing",
                            details={"expected_replicas": bot.replicas},
                        )
                    )
            return drifts

        actual: Dict[str, Any] = inventory.get_bot_inventory()
        actual_bots: Dict[str, Any] = {
            b["name"]: b for b in actual.get("all_bots", [])
        }
        desired_names = {b.name for b in manifest.bots if b.enabled and b.replicas > 0}

        for bot in manifest.bots:
            if not bot.enabled or bot.replicas == 0:
                continue
            if bot.name not in actual_bots:
                drifts.append(
                    DriftItem(
                        bot_id=bot.bot_id,
                        drift_type="missing",
                        details={"expected_name": bot.name},
                    )
                )
            else:
                actual_bot = actual_bots[bot.name]
                if actual_bot.get("role") != bot.role:
                    drifts.append(
                        DriftItem(
                            bot_id=bot.bot_id,
                            drift_type="config_mismatch",
                            details={
                                "field": "role",
                                "desired": bot.role,
                                "actual": actual_bot.get("role"),
                            },
                        )
                    )

        # Extra bots not in desired state
        for actual_name in actual_bots:
            if actual_name not in desired_names:
                drifts.append(
                    DriftItem(
                        bot_id=actual_bots[actual_name].get("agent_id", actual_name),
                        drift_type="extra",
                        details={"actual_name": actual_name},
                    )
                )

        return drifts


# ---------------------------------------------------------------------------
# FleetReconciler
# ---------------------------------------------------------------------------

class FleetReconciler:
    """
    Reconciliation engine: converges actual fleet state to declared desired state.

    Parameters
    ----------
    bot_inventory:
        BotInventoryLibrary instance used to spawn/despawn bots.
    heartbeat_monitor:
        Optional object with a register_policy(bot_id, policy) method.
    supervision_tree:
        Optional SupervisionTree instance for wiring topology.
    event_backbone:
        Optional EventBackbone for publishing audit events.
    persistence_manager:
        Optional PersistenceManager for durable state snapshots.
    """

    def __init__(
        self,
        bot_inventory: Any,
        heartbeat_monitor: Optional[Any] = None,
        supervision_tree: Optional[Any] = None,
        event_backbone: Optional[Any] = None,
        persistence_manager: Optional[Any] = None,
    ) -> None:
        self._bot_inventory = bot_inventory
        self._heartbeat_monitor = heartbeat_monitor
        self._supervision_tree = supervision_tree
        self._event_backbone = event_backbone
        self._persistence_manager = persistence_manager

        self._manifest: Optional[FleetManifest] = None
        self._actions: List[ReconciliationAction] = []
        self._lock = threading.Lock()

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def load_manifest(self, manifest: FleetManifest) -> None:
        """Load the desired-state manifest."""
        with self._lock:
            self._manifest = manifest
            self._actions = []
        logger.info(
            "FleetReconciler: manifest '%s' v%s loaded (%d bots)",
            manifest.fleet_id,
            manifest.version,
            len(manifest.bots),
        )

    def observe(self) -> Dict[str, Any]:
        """Snapshot actual state from the bot inventory."""
        return self._bot_inventory.get_bot_inventory()

    def diff(self) -> List[ReconciliationAction]:
        """
        Compare desired manifest vs actual inventory and return a list of
        ReconciliationActions needed to converge.
        """
        with self._lock:
            manifest = self._manifest

        if manifest is None:
            return []

        actual = self.observe()
        actual_by_name: Dict[str, Any] = {
            b["name"]: b for b in actual.get("all_bots", [])
        }

        actions: List[ReconciliationAction] = []
        ordered_bots = _topological_sort(manifest.bots)

        for bot in ordered_bots:
            if not bot.enabled or bot.replicas == 0:
                # If disabled but exists → DESPAWN
                if bot.name in actual_by_name:
                    actions.append(
                        ReconciliationAction(
                            action_type=ActionType.DESPAWN,
                            target_bot_id=bot.bot_id,
                            details={
                                "name": bot.name,
                                "agent_id": actual_by_name[bot.name]["agent_id"],
                                "reason": "disabled_in_manifest",
                            },
                        )
                    )
                continue

            if bot.name not in actual_by_name:
                actions.append(
                    ReconciliationAction(
                        action_type=ActionType.SPAWN,
                        target_bot_id=bot.bot_id,
                        details={
                            "name": bot.name,
                            "role": bot.role,
                            "capabilities": bot.capabilities,
                            "replicas": bot.replicas,
                        },
                    )
                )
            else:
                actual_bot = actual_by_name[bot.name]
                if actual_bot.get("role") != bot.role:
                    actions.append(
                        ReconciliationAction(
                            action_type=ActionType.UPDATE,
                            target_bot_id=bot.bot_id,
                            details={
                                "agent_id": actual_bot["agent_id"],
                                "name": bot.name,
                                "field": "role",
                                "desired": bot.role,
                                "actual": actual_bot.get("role"),
                            },
                        )
                    )

            # Always ensure heartbeat policy is registered
            actions.append(
                ReconciliationAction(
                    action_type=ActionType.REGISTER_HEARTBEAT,
                    target_bot_id=bot.bot_id,
                    details={
                        "interval_seconds": bot.heartbeat_policy.interval_seconds,
                        "max_missed": bot.heartbeat_policy.max_missed,
                        "recovery_strategy": bot.heartbeat_policy.recovery_strategy,
                    },
                )
            )

        # Supervision wiring
        with self._lock:
            topology = manifest.supervision_topology if manifest else {}
        for supervisor_id, children in topology.items():
            actions.append(
                ReconciliationAction(
                    action_type=ActionType.WIRE_SUPERVISION,
                    target_bot_id=supervisor_id,
                    details={"supervisor_id": supervisor_id, "children": children},
                )
            )

        # Extra bots in actual that are NOT in manifest → DESPAWN
        desired_names = {b.name for b in manifest.bots}
        for name, actual_bot in actual_by_name.items():
            if name not in desired_names:
                actions.append(
                    ReconciliationAction(
                        action_type=ActionType.DESPAWN,
                        target_bot_id=actual_bot["agent_id"],
                        details={
                            "agent_id": actual_bot["agent_id"],
                            "name": name,
                            "reason": "not_in_manifest",
                        },
                    )
                )

        with self._lock:
            self._actions = actions
        return actions

    def reconcile(self) -> Dict[str, Any]:
        """
        Execute the diff actions to converge actual state to desired state.

        Returns a status summary dict.
        """
        self._publish(
            "FLEET_RECONCILIATION_STARTED",
            {"fleet_id": self._manifest.fleet_id if self._manifest else "unknown"},
        )

        actions = self.diff()
        completed = 0
        failed = 0

        for action in actions:
            try:
                self._execute_action(action)
                action.status = ActionStatus.COMPLETED
                completed += 1
            except Exception as exc:
                logger.error(
                    "FleetReconciler: action %s failed: %s",
                    action.action_id,
                    exc,
                )
                action.status = ActionStatus.FAILED
                failed += 1

        fleet_id = self._manifest.fleet_id if self._manifest else "unknown"
        self._publish(
            "FLEET_RECONCILED",
            {
                "fleet_id": fleet_id,
                "total_actions": len(actions),
                "completed": completed,
                "failed": failed,
            },
        )

        return {
            "fleet_id": fleet_id,
            "total_actions": len(actions),
            "completed": completed,
            "failed": failed,
            "actions": [a.to_dict() for a in actions],
        }

    def get_reconciliation_status(self) -> Dict[str, Any]:
        """Return convergence progress of the last reconciliation run."""
        with self._lock:
            actions = list(self._actions)
            manifest = self._manifest

        total = len(actions)
        completed = sum(1 for a in actions if a.status == ActionStatus.COMPLETED)
        failed = sum(1 for a in actions if a.status == ActionStatus.FAILED)
        pending = sum(1 for a in actions if a.status == ActionStatus.PENDING)

        return {
            "fleet_id": manifest.fleet_id if manifest else None,
            "manifest_version": manifest.version if manifest else None,
            "total_actions": total,
            "completed": completed,
            "failed": failed,
            "pending": pending,
            "converged": total > 0 and failed == 0 and pending == 0,
            "actions": [a.to_dict() for a in actions],
        }

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _execute_action(self, action: ReconciliationAction) -> None:
        """Dispatch a single ReconciliationAction to the appropriate handler."""
        if action.action_type == ActionType.SPAWN:
            self._do_spawn(action)
        elif action.action_type == ActionType.DESPAWN:
            self._do_despawn(action)
        elif action.action_type == ActionType.UPDATE:
            self._do_update(action)
        elif action.action_type == ActionType.REGISTER_HEARTBEAT:
            self._do_register_heartbeat(action)
        elif action.action_type == ActionType.WIRE_SUPERVISION:
            self._do_wire_supervision(action)

    def _do_spawn(self, action: ReconciliationAction) -> None:
        d = action.details
        bot = self._bot_inventory.spawn_bot(
            name=d["name"],
            role=d["role"],
            capabilities=d.get("capabilities"),
        )
        self._publish(
            "FLEET_BOT_SPAWNED",
            {
                "bot_id": action.target_bot_id,
                "agent_id": bot.agent_id,
                "name": d["name"],
                "role": d["role"],
            },
        )
        logger.info("FleetReconciler: spawned bot '%s' (%s)", d["name"], bot.agent_id)

    def _do_despawn(self, action: ReconciliationAction) -> None:
        agent_id = action.details.get("agent_id", action.target_bot_id)
        self._bot_inventory.despawn_bot(agent_id)
        self._publish(
            "FLEET_BOT_DESPAWNED",
            {"bot_id": action.target_bot_id, "agent_id": agent_id},
        )
        logger.info("FleetReconciler: despawned bot '%s'", agent_id)

    def _do_update(self, action: ReconciliationAction) -> None:
        # For a live role change: despawn and re-spawn with the correct role.
        d = action.details
        agent_id = d.get("agent_id", "")
        self._bot_inventory.despawn_bot(agent_id)
        bot = self._bot_inventory.spawn_bot(
            name=d["name"],
            role=d["desired"],
        )
        self._publish(
            "FLEET_BOT_UPDATED",
            {
                "bot_id": action.target_bot_id,
                "new_agent_id": bot.agent_id,
                "field": d.get("field"),
                "desired": d.get("desired"),
            },
        )
        logger.info(
            "FleetReconciler: updated bot '%s' field '%s' → '%s'",
            d.get("name"),
            d.get("field"),
            d.get("desired"),
        )

    def _do_register_heartbeat(self, action: ReconciliationAction) -> None:
        if self._heartbeat_monitor is None:
            return
        d = action.details
        try:
            self._heartbeat_monitor.register_policy(action.target_bot_id, d)
        except AttributeError as exc:
            logger.debug(
                "FleetReconciler: heartbeat_monitor has no register_policy: %s", exc
            )

    def _do_wire_supervision(self, action: ReconciliationAction) -> None:
        if self._supervision_tree is None:
            return
        d = action.details
        try:
            from supervision_tree import ChildSpec, SupervisionStrategy, SupervisorNode

            supervisor_id = d["supervisor_id"]
            children = d.get("children", [])
            child_specs = [
                ChildSpec(child_id=cid, start_fn=lambda: None)
                for cid in children
            ]
            node = SupervisorNode(
                supervisor_id=supervisor_id,
                strategy=SupervisionStrategy.ONE_FOR_ONE,
                children=child_specs,
            )
            self._supervision_tree.register_supervisor(node)
        except Exception as exc:
            logger.warning(
                "FleetReconciler: supervision wiring failed for '%s': %s",
                action.target_bot_id,
                exc,
            )

    def _publish(self, event_name: str, payload: Dict[str, Any]) -> None:
        """Publish an event to the EventBackbone (best-effort)."""
        if self._event_backbone is None:
            return
        try:
            from event_backbone import EventType
            event_type = EventType(event_name.lower())
            self._event_backbone.publish(event_type, payload, source="fleet_reconciler")
        except Exception as exc:
            logger.debug("FleetReconciler: event publish skipped: %s", exc)
