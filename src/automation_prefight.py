"""
PATCH-177: Automation Pre-Fight Engine — automation_prefight.py

Pre-fight is the intelligence gate that runs BEFORE any browser automation task.
It answers one question: "Which engine should handle this, and why?"

Decision tree:
  1. Probe target URL → detect captcha type
  2. Check engine availability (GhostController CDP, Playwright)
  3. Apply routing rules based on captcha type + engine capability matrix
  4. Return: AutomationMethod + confidence + rationale

Routing rules (in priority order):
  ├── hCaptcha present               → GHOST (CDP/real JS — hCaptcha token valid)
  ├── Cloudflare Turnstile present   → GHOST (real browser fingerprint needed)
  ├── reCAPTCHA v2/v3 present        → PLAYWRIGHT (audio fallback + stealth JS)
  ├── No captcha detected            → PLAYWRIGHT (faster, more reliable selectors)
  ├── Ghost unavailable              → PLAYWRIGHT (fallback)
  └── Both unavailable               → HITL_ESCALATE (human takes over)

Why this split:
  GhostController (CDP):
    - Drives a real Chromium process via DevTools Protocol
    - hCaptcha token is generated by real in-browser JS → passes validation
    - No Playwright fingerprint markers → harder to detect as bot
    - Slower to launch, heavier resource usage
    - Best for: Stripe, sites with hCaptcha, fingerprint-heavy targets

  Playwright:
    - Clean API, stealth JS patches (navigator.webdriver=false)
    - Better at DOM selector reliability and form filling
    - Audio CAPTCHA fallback for reCAPTCHA
    - Faster cold start
    - Best for: sites with reCAPTCHA or no captcha, form-heavy flows

Murphy's Law applied: Never let the wrong tool try first.
If the probe fails, assume the worst captcha scenario.
"""
